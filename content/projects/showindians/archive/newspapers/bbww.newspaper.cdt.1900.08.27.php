		<h1>&quot;BUFFALO BILL&quot; IS HERE.</h1>

		<h3>WILD WEST SHOW WILL ENTERTAIN THE VETERANS.</h3>

		<h3>Opening Performance Is Given in the Rain&mdash;Thousands Attend&mdash;Old Soldiers Are Carried Away by Military Ardor When Charge of San Juan Begins and Start Out to Help Whip the Spaniards&mdash;Colonel Cody Takes Part.</h3>

		<p>Buffalo Bill, at the head of his Wild West show, or Colonel William F. Cody, leading his military pageant, the name has a charm which is irresistible.  It brought out <span title="BBWW: Attendance" style="background-color: transparent;">thousands of people yesterday</span>, one of the most dismal of August Sundays, to witness the opening performance for Grand Army week.  In honor of the encampment the spectacle has been made more military than ever, with none of the wildness of its Western features omitted.</p>

		<p><span title="BBWW: Genuine" style="background-color: transparent;">The weather only gave the scenes from camp life and battlefield a more realistic appearance.  The soldiers pitching their tents in rain and mud and kindling campfires under difficulties brought back to the veteran who watched it many scenes half forgotten</span>.</p>

		<h3>Performers Ride in Rain.</h3>

		<p>The show is given on vacant lots at Wentworth avenue and Thirty&ndash;fifth street.  The ground is below the street level, and in spite of the fact that shavings and straw had been used in quantities the arena was in a state of partial inundation.  The spectators were housed under canvas, but the performers rode through rain and mud.</p>
		 
		<p>The show would lack the most amusing feature of yesterday?s performance in dry weather.  The antics of the Turkish tumblers proved strangely ludicrous under the circumstances.  The man who moves down the arena by springing from his hands and feet in such rapid succession that he looks like a wheel with two spokes resembled the side wheel of a steamer as he churned up the water, mud, and sawdust through which he was revolving.  The crowds substituted laughter for the usual applause.</p>

		<p>The charge up San Juan Hill is conducted by men who were with Colonel Roosevelt.  At one end of the long arena is the wall which protects the Spanish troops.  An American scout is seen, and as he advances slowly he is fired upon by t he picket line of the Spaniards.  The charge then begins in earnest as a company of Rough Riders appears, dismounted as they were at San Juan.</p>

		<h3>Veterans Start for Firing Line.</h3>

		<p>The rapid&ndash;fire gun which is brought into action in the charge is one of the new features of military spectacles.  It was new to the veterans who served in a war when to keep a gun going at intervals of three or four minutes was considered a feat.  The rapid&ndash;fire gun pours out a steady stream of fire that centers attention upon it.</p>

		<p>Backed by the steady rattle of musketry and burled under the occasional boom of a big gun from the fort it seemed the dominating feature on the field.  The police had to go out in pursuit of some of the G. A. R. uniform hats which were seen bobbing along through the smoke that covered the field.  They brought back several grizzled veterans who had entirely forgotten themselves in the excitement and had started for the firing line.</p>

		<p>A veteran gunner of the Thirty&ndash;fourth Michigan stood by the ropes.  &quot;What do you think of that gun&quot; he shouted, pointing toward the rapid&ndash;fire piece.  &quot;If we could have poked it into ?em at that rate it wouldn't have taken so long to get up Lookout Mountain.&quot;</p>

		<h3>Indians Choose Dry Spots.</h3>

		<p><span title="NA: Character" style="background-color: transparent;">The Indians were disgusted with the mud and performed their part rather fastidiously, choosing dry spots between the mud holes to do their dance in.  &quot;There is nothing that hates mud and water like an Indian with his war paint on,&quot; explained one of the employes.  &quot;He is as bad as a house cat, he doesn't even want to get his feet wet.&quot;</span></p>

		<p>Colonel Cody takes his usual part, breaking glass balls while riding his horse around the ring, leading the United States Cavalry, which rescues a stage coach from the Indians, and, finally, reviewing the pageant that closes the show.  Colonel Cody is to ride in the parade today as a Marshal of one of the divisions.</p>

		<p>The Wild West show will remain in the city for one week.  The Wild West show has been marked by a peculiar development, preserving all features that were characteristic when it first started, and at the same time adding features which illustrate the progress of American history since that time.</p>

			
