		<h1>RED MAN'S TROUBLES AIRED</h1>

		<h3>Buffalo Bill's Indians Have an Interview With the Assistant Commissioner.</h3>

		<h3>They Were Promised a Rigid Investigation Into the Matters by Proper Agents.</h3>

		<p>Washington World&ndash;Herald Bureau<br/>
		Room 23 Post Building.<br/>
		Washington, D.C., Oct. 4.</p>

		<p>A conference held yesterday between Assistant Indian Commissioner Smith and Buffalo Bill's Sioux Indians showed that the Indians have many grievances against the government concerning the affairs at the reservation.  <span title="NA: Character" style="background-color: transparent;">Many questions were propounded by Rock Bear which afforded additional proof of the willy characteristics of the Sioux</span>.  As a result of the conference an investigation will be made as to the right of Jarlett Richards, a beef contractor, to graze 4,000 or more head of cattle on the Sioux reservation, and a promise was secured that no more of the old Indians of their tribe would be arrested under the Edmunds antipolygamy act, such arrests being a source of revenue to United States officers, but it was decided that the Indian agent in charge should endeavor to prevent the younger Indians from taking unto themselves more than one wife.  Rock Bear's assertion that further arrests under this act would be made in the Black Hills exacted the promise that the matter would be looked into.</p>

