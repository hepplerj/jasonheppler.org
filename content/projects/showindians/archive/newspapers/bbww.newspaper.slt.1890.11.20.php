		<h1>THE BUFFALO BILL CHIEFS.</h1>

		<h3>They Think It Folly to Fight Against the Pale Faces.</h3>

		<p>A <i>Bee</i> special from Pilger, Neb., says: <span title="NA: Members" style="background-color: transparent;">Two special coaches from Washington, containing forty&ndash;five of Buffalo Bill's Indians, passed through here to&ndash;today en route to Pine Ridge agency</span>.  All have friends and relatives there.  <span title="NA: Members" style="background-color: transparent;">Rocky Bear, Black Heart, Little Wolf, Black Elk</span> and other prominent chiefs are among them.  <span title="Progressivism: Indian Problem" style="background-color: transparent;">Several of the party had received letters from Pine Ridge, <span title="Context: Indian Wars" style="background-color: transparent;">speaking of the Messiah</span>, <span title="BBWW: Travel" style="background-color: transparent;">before they sailed from Europe</span></span>.</p>

		<p><span title="Progressivism: Indian Problem" style="background-color: transparent;">Through an interpreter Rock Bear said he thought his party could do more than any others to quiet their brothers at Pine Ridge.  &quot;My people,&quot; he said, &quot;may get crazed, as white people do, over religion.  If so, I am sorry.  We here know that the day for the red man to fight against the pale face is long past.&quot;</span></p>

