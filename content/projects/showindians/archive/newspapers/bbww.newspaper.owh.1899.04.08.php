		<h1>LIMMINGS.</h1>

		<p>Visitors to the exposition last year who spent any time at the Indian department will entertain <span title="Image: Civilization" style="background-color: transparent;"><span title="NA: Members" style="background-color: transparent;">pleasant recollections of John Hollow Horn Bear.  John is a stalwart Indian who has succumbed in a measure to the enervating influences of civilization</span></span>.</p>

		<p>John Hollow Horn Bear is very anxious to make another visit to Omaha and pose before admiring crowds.  He is much interested in the Greater America exposition and has written to W. H. Liddlard &#8212; &quot;Rattlesnake Peat&quot; &#8212; in reference thereto John writes.</p>

		<p>Rosebud Agency, March 20 &#8212; W. A. Liddlard (Pete) &#8212; Dear Sir, Your kind letter was received and was glad to hear from you.  And I am going to answer it after a few minutes and I would like to go to the exposition again, and you told me you would like to, I am looking for some good men.  So I am glad of it.  I am sure some of the good men at this reservation.  So I could found some good and I wish you would let me know if the Indian have pay for in that exposition.  Then I looking for the good men and <span title="BBWW: Business" style="background-color: transparent;">I heard about Buffalo Bill, He come to the agency for to get some good men.  He take them for Wild West show.  He shall come over on April 1, 1899</span>.  Well now I put some good mens names on the other papers:</p>
			
