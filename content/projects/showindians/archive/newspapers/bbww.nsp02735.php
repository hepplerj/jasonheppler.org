		<h1>THE WILD WEST SHOW.</h1>

		<h3>Notes of Interest About the Exhibition and Buffalo Bill's Visitors.</h3>

		<p><span title="BBWW: Attendance" style="background-color: transparent;">The attendance yesterday excelled the opening day</span>.</p>

		<p>An excursion from Reading and intermediate stations is due to&ndash;morrow.</p>

		<p>A gentleman took a fractious horse to the show yesterday to be broken and ridden.  After a hard struggle the animal was conquered by Jim Kidd, and turned over to the owner well broken and gentle.</p>

		<p>General Sandy Forsyth, formerly on General Sheridan's staff, was at the Wild West yesterday visiting his old scout and army friend, Colonel Cody.  He was well pleased with the entertainment, and expressed delight at the enthusiastic manner in which the <span title="BBWW: Genuine" style="background-color: transparent;">vivid scenes of the soul-stirring days of the frontier were witnessed by the patrons</span>.</p>

		<p>One hundred and seventy five Indian scholars from Carlisle enjoyed yesterday's exhibition.  Many of their fathers and relatives from the Pine Ridge Agency are now with Buffalo Bill.  <span title="Progressivism: Reform" style="background-color: transparent;"><span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">One of the curiosities of Indian history came to light when Blue Horse, one of the Indian chiefs with the Wild West, met his son, who has been a number of years at Carlisle.  The son could not speak Sioux, and Blue horse could not speak English, so the aid of an interpreter was called in to enable them to converse</span></span></span></span>.</p>

