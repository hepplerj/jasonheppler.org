		<h1>BECOMING CIVILIZED.</h1>

		<h3>Buffalo Bill's Sioux Chieftains Show the Influences of Travel.</h3>

		<p>From the New York Times.</p>

		<p>The last complement of Indians for Buffalo Bill's Wild West show, which opens in the Madison Square Garden next week, passed through town yesterday on their way to Bridgeport, Conn., where the outfit is assembling.  There were a hundred Sioux warriors fifteen squaws, and as many pappooses.  They came from the Pine Ridge and Rosebud agencies, in South Dakota, where they had been chosen by Scout Bill McCune.</p>

		<p>They first appeared on the public view early last evening in the Erie station, in Jersey City, where, alighting from a train, they sprinted for a ferryboat.  A number of commuters hastening in to &quot;sacred concerts&quot; were likewise engaged.</p>

		<p>&quot;The Indian is rapidly becoming civilized,&quot; panted a commuter who was running beside Iron Tail and William Sitting Bull, the principal chiefs.</p>

		<p>On the way over the Indians divided naturally into groups, the principal chiefs being with Rocky Bear.  Four of those in the outfit have become fast friends.  They were with the show during its four years European trip.  When they first returned to these shores they showed slight traces of foreign accent.  This, however, has been abandoned and they are now citizens of the world.</p>

		<p>&quot;I believe, after a casual survey of Europe,&quot; said Young&ndash;Man&ndash;Not&ndash;Afraid&ndash;of&ndash;a&ndash;Thirst, one of the globe trotters, &quot;that the highest ideal of civilization finds its individual embodiment in that man who speaks each language as though he were a native.  You see, my English is without fault, my French is better, but I've forgotten my Indian.&quot;</p>

		<p>&quot;I quite agree with you,&quot; said Standing Bill, another of the four, so named because he sympathetically forgives the European fad of letting bills stand, &quot;but each time I return to my native land I see anew the errors of our boasted American civilization.  We are too commercial over here.  We give up too much of our minds toward the seeking of mineral things.  Art we leave to Europe.  All things are on a commercial basis.  If a man fails in business, we call him a 'failure,' when in true character and right living he may be anything but a failure.&quot;</p>

		<p>John Bull&ndash;Bear, another of the world citizens, who occasionally takes a flier in Wall street, announced that he had something to say on the traction situation.</p>

		<p>&quot;I have made a careful comparative study of the modes of traction abroad,&quot; he said, &quot;especially with such as have to do with the transportation of persons within a great city.  I should strongly advise the investing public to invest in the London underground in preference to our own subway.  Over there the cars are fireproofed by a liberal use of asbestos, while here even the steel cars would burn inside.  But I will say that our tunnel excavators are showing rare resource in their recent work.&quot;</p>

		<p>While Young&ndash;Man&ndash;Not&ndash;Afraid&ndash;of&ndash;a&ndash;Thirst and Standing Bill were arguing warmly as to the proper length of time a truffled grouse should be cooked, the boat was made fast to her slip.</p>

		<p>Prodded by the scouts, the Sioux were urged off the deck.  On the shore at Twenty-third street two large sightseeing automobiles labeled &quot;Seeing New York&quot; were in waiting.  The four, with other Indians, mounted together into one of them and the two machines went east through Twenty&ndash;third street.</p>

		<p>All of the Sioux, except the cosmopolitan four, had never left their reservations before.  So they put the autos to the use for which they were originally intended.</p>

		<p>The sight of the Pennsylvania series of excavations moved Standing Bill to comment.</p>

		<p>&quot;Why, four years ago,&quot;  he said, &quot;I had the tip, that the Pennsylvania people in turn, but I didn't believe it.  Besides, I was pretty deep in Swamphurst and Lonesomehurst properties.  I have just dropped a few, from the looks of things.&quot;</p>

		<p>&quot;Never mind,&quot; said Young&ndash;Man&ndash;Not&ndash;Afraid&ndash;of&ndash;a&ndash;Thirst, with biting irony.  &quot;This spring you can buy one of the Harriman railway properties cheap and develop it.&quot;</p>

		<p>In Broadway, the altered appearance of the Gilsey House struck John Bull&ndash;Bear.</p>

		<p>&quot;By Jove, the Gilsey House has changed hands again,&quot; he said.</p>

		<p>At the Grand Central station the Sioux were late and were hurried to a train.  Young&ndash;Man&ndash;Not&ndash;Afraid&ndash;of&ndash;a&ndash;Thirst was the first to discover that there was no buffet car attached to the train.  He already knew there are no &quot;Last Chance&quot; signs in New York.</p>

		<p>&quot;It is a beastly oversight,&quot; he complained.  &quot;Why, I haven't had a thing to eat since morning except a caviar sandwich.&quot;</p>

