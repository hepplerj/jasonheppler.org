		<h1>Don't Agree With Them.</h1>

		<p><span title="NA: Members" style="background-color: transparent;">Five red Americans of the Dakota tribe of Sioux, who have been assisting Buffalo Bill, got here yesterday on the steamship Saale</span>.  They came in the steerage with a lot of foreigners. <span title="NA: Treatment" style="background-color: transparent;">One of them, <span title="NA: Description" style="background-color: transparent;">a tall, little-limbed buck</span> of IS, was so ill that General O'Beirne, who is an old plainsman and understands the Sioux language, had him sent to Bellevue Hospital.  His right arm from the wrist to the elbow is badly ulcerated.  The arm, according to the hospital surgeon who examined it, looks as if it had been broken and not properly attended to</span>.</p>

		<p>It gave the young Indian great pain, which he bore with <span title="NA: Character" style="background-color: transparent;">smiling stoicism</span>.  Besides this ailment he has consumption.  His name is Kill His Pony.  <span title="NA: Treatment" style="background-color: transparent;">None of the Indians looked as if they had had an altogether Pleasant time abroad.  All but Kill His Pony, who probably will never come out of Bellevue alive, start tomorrow to join their tribe at Pine Ridge Agency</span>.  Kill His Pony had a severe hemorrhage just after landing at the barge office.</p>

