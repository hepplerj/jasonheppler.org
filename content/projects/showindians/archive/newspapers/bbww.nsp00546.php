		<h1>The Wild West Visits the War Department in War Paint.</h1>

		<p>Buffalo Bill, accompanied by Sitting Bull and fifteen Indians, called at the War Department and paid their respects to General Sheridan and Adjutant General Drum.  The Indians wore their war costume.  <span title="NA: Descriptions" style="background-color: transparent;">Their faces were embellished with red and yellow paint, on their heads they wore immense feathers.  Sitting Bull's head was adorned by a number of feathers of large size</span>.  In General Sheridan's room but little conversation was indulged in.  <span title="NA: Descriptions" style="background-color: transparent;">Sitting Bull gave an occasional grunt when spoken to by an Indian companion.  He paid but little attention his surroundings.  The other Indians were interested in pictures of Indian life that adorned the walls.  They paid especial attention to a buffalo scene, calling the attention of each other to see, talked and laughed among themselves</span>.</p>

		<p>The Indians left the room in single file and passed about a hundred War Department clerks who stood in the corridors.  The visit to General Drum was brief&mdash;only a formal introduction taking place there.  Before leaving the State, War and Navy Department building the party visited the State Department library and examined the original copy of the Declaration of Independence and other relics.</p>

		<p>The party next paid their respects to Secretary Bayard, who received them very pleasantly and then went to the White House where they were joined by Nate Salsbury and John M. Burke.  The President received the company in the library, where a grand handshaking took place, but there was no speeches.  <span title="NA: Descriptions" style="background-color: transparent;">Sitting Bull said, however, that he was delighted with his trip East and wished he had seen all this when he was a boy</span>.  At the Interior Department Secretary Lamar and Indian Commissioner Atkins shook hands all around and indulged in a short conversation with the different members of the party.</p>

