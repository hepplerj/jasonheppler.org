		<h1>BUFFALO BILL'S SURPRISE.</h1>

		<h3>How He Made Some Poor Little Invalids In Boston Happy.</h3>

		<p>When the Wild West visited Boston one hot june day the parade passed a children's hospital on the way to the show grounds.  Many of the little invalids were unable to leave their couches.  All who could do so ran to the open windows and gazed eagerly at the passing procession, and the greatest excitement prevailed.  These more fortunate little ones described as best they could in the little sufferers who could not leave their beds the wonderful things they saw.  The Indians were the special admiration of the children.  After the procession passed, one wee lad, bedridden by spinal trouble, cried bitterly because he had not seen it.  A kind hearted nurse endeavored to soothe the child, but words proved unavailing.  Then a bright idea struck the patient woman.  She told him he might write a letter to the great Buffalo Bill himself and ask for an Indian's picture.</p> 

		<p>This idea was taken up with delight and the child spent an eager hour in penning the letter.  It was pathetic in its simplicity.  The little sufferer told the great showman that he was sick in bed and unable to see the Indians, and he longed to see them when they passed the hospital, and that he wished to see a photograph of one.</p>

		<p>The important missive was mailed and even the impatient little invalid knew it was useless to expect an answer that day.  The morning had hardly dawned before the child's bright eyes were open.  Every noise he listened to, and he wondered when the postman would bring him a letter.  The nurse hardly dared to hope that a busy man like Buffalo Bill would have time to respond to the wish of a sick child.  &quot;Colonel Cody is a very busy man,&quot; she said.  &quot;We must be patient.&quot;</p>

		<p>At perhaps the twentieth repetition of this remark the door opened noiselessly.  <span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Noble Savage" style="background-color: transparent;">In came a six-foot Indian clad in leather trousers and wrapped in a scarlet blanket. He wore a headdress of tall, waving feathers and carried his bow in his hand</span></span>.</p>

		<p>The little invalids gasped in wonder, then they shrieked with delight.  <span title="NA: Descriptions" style="background-color: transparent;">One by one, silent and noiseless but smiling, six splendid warriors followed the first.  The visitors had evidently been well trained, and had received explicit directions as to their actions</span>. So unusual a sight in the orderly hospital so startled the nurse that she could not even speak.  The warriors drew up in a line and saluted her.  The happy children were shouting in such glee that the poor woman's fright was unnoticed.</p>

		<p><span title="NA: Performances" style="background-color: transparent;">The Indians ranged themselves in the narrow space between the cots, laid aside their gay blankets, placed their bows upon the floor, and, waving their arms to and fro, executed a quiet war dance.  A sham battle was fought, followed by a song of victory.  After this the blankets were again donned, the kindly red men went away, still smiling as benignly as their war paint would allow them to do</span>.  A cheer of gratitude and delight followed them down the broad corridors.  The happy children talked about Buffalo Bill and the Wild West show for weeks after this visit.  &#8212;<em>Last of the Great Scouts</em>.</p>

