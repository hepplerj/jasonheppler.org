		<h1>INDIANS IN THE WILD WEST SHOW</h1>

		<h3>Even When Not Performing They Wear Native Dress.</h3>

		<h3>Major Burke Tells About Their Habits and How They Are Kept from Drinking and Fighting.</h3>

		<p><span title="Image: Savagery" style="background-color: transparent;">The Indian of the story book and of history is a fierce visage, vindictive, untamable human being</span>.  <span title="Image: Civilization" style="background-color: transparent;">The redskin of the Wild West Show is an entirely different person, yet there is in him all the latent energy, all the ungovernableness of his kin of the story book.  In him, however, it is repressed.  How all that is done is an interesting story.  Buffalo Bill was many years in mastering the art</span>.</p>

		<p><span title="NA: Descriptions" style="background-color: transparent;">Col. Cody's Indians are mild&ndash;mannered, harmless, and as gentle as Mohicans, notwithstanding the fact that they are for the most part of the famous Sioux tribe, and that <span title="Context: Custer Massacre" style="background-color: transparent;">Iron Tail, the chief of Buffalo Bill's aggregation, was one of the participants in the Custer massacre</span></span>.  <span title="BBWW: Travel" style="background-color: transparent;">Sixty Indians travel with the Wild West Show</span>, and Col. Cody said last week that in all his seventeen years' experience he had never had a serious difficulty with any of them.</p>

		<p>The Indian of the show is very little different behind the scenes from what he is before the curtain.  When on the road <span title="Image: Noble Savage" style="background-color: transparent;">he lives in wigwams, brought from his native heath, just like those quickly made ones displayed in the show.  He always wears the same costume he exhibits in the show, and off the stage he revels in the same fantastic painting of his face and body</span>.</p>

		<p>Major John M. Burke, himself from Indian Territory, secures most of the Indians for Col. Cody year by year.  In speaking of the love of the red man for color, Major Burke said to a New York Times reporter a few days ago:</p>

		<p>&quot;I am often asked if the Indian's make&ndash;up for the show is his own. Indeed it is. No pale face could ever devise such curious, quaint, and significant arrangements of paint and cork. All that the Indian puts on his face and body means something.It tells a tale of war, of peace, of happiness, of misery.</p>

		<p>&quot;The Indian at his own home paints his face for three reasons.  In the first place, he wants to disguise himself, so that if he ever meets a man in combat he will not be recognized again, if he changes his make&ndash;up.  He also wants to strike terror to his enemy, and he thinks he can add to his impression if he scowls behind a conglomeration of horrid figuring.</p>

		<p>&quot;The red man varies his coloring with the seasons.  A third value to him of his paint is in the ability it gives him to skulk in and out of the streets without being discovered.  In Spring he will paint himself green; in Fall a brown or golden color.  He does this so that he cannot be distinguished from the plant growth as he trips here and there among it.</p>

		<p>&quot;The Indian's garments are his own, too.  It is true that he now often wears clothing woven by the pale face, but in his own home, in the days of his prime, the Indian?s garments were wondrously rich and beautiful.  The poor red man?s riches have been taken away from him, however, and he now glories in what is but a shadow of his past magnificence.&quot;</p>

		<p>The common conception is that the Indian is a great consumer of whiskey.  It is often asked how Buffalo Bill keeps his red men away from &quot;fire water,&quot; for the propensities of the Indian in this respect no one questions.  Major Burke, on this subject, said:</p>

		<p><span title="Progressivism: Reform" style="background-color: transparent;">&quot;All of our Indians are under contract with us.  When we take them from the reservations they sign an agreement to obey orders, to refrain from all drinking, gambling, and fighting</span>.  In order to make them stand by their agreement we have a sort of home rule among them.  For every dozen Indians there is an Indian policeman.  He wears a badge, and is paid $10 a month more than the rank and file.  This policeman is elected by the twelve, and he can be deposed by them at any time.</p>

		<p>&quot;This policeman is responsible for the conduct of those under him.  He watches them carefully all the time, and warns them if he sees trouble brewing.  There is a chief of police among them, and over him sits Iron Tail, whose authority is not questioned.  He is amenable alone to Col. Cody.</p>

		<p>&quot;<span title="Progressivism: Reform" style="background-color: transparent;">We teach the Indians from the time we take them that a saloon is a very bad place for an Indian. We tell him that even if he doesn't drink it is a bad place to loaf.  It is done with kindness, and we seek to persuade as much as possible</span>.  We find that this plan works beautifully, and in our career we have found but few instances of Indians' drinking.  Of course, <span title="NA: Character" style="background-color: transparent;"><span title="Image: Savagery" style="background-color: transparent;">he is a dangerous beast when he does drink, and we have to exercise the greatest care</span></span>.</p>

		<p>&quot;The Indian does no fighting either.  We seek to keep from ruffling him in every way possible.  We give him plenty to eat, and keep him as comfortable as possible.  This keeps his temper calm.  Few of the red men can talk English, so they don?t get into disputes with the other members of our aggregation.</p>

		<p>&quot;It is, in fact, remarkable, considering the wild, rough&ndash;and&ndash;ready aggregation that we have, that there is not more fighting and brawling.  Our men are directly from the plains, and they do not run away from fights.  But this fact is to be taken into consideration&mdash;they understand one another and they know they will get all they give if there is any fighting done.  This probably acts to keep their humors pleasant and manageable.&quot;</p>

		<p><span title="NA: Descriptions" style="background-color: transparent;">It was a long time, according to Major Burke, after Col. Cody began taking Indians around with him before these redmen could understand why it was that when they&mdash;only 100 strong&mdash;were placed in the centre of say 10,000 spectators the redmen were not massacred</span>.  They knew what they would have done had converse conditions prevailed, and they thought it miraculous that they escaped.</p>

		<p><span title="Context: Indian Wars" style="background-color: transparent;">They went home and told their tribesmen of this wonderful mercy, and the Major thinks the contact these men have had with civilization has had a remarkably pacifying influence upon the whole Indian race in America</span>.</p>

		<p><span title="Progressivism: Reform" style="background-color: transparent;">&quot;It has had an educating influence upon them, too,&quot; continued Major Burke.  &quot;They, of course, do not understand all that they see as they travel around the world, but they catch on to a lot of it.  Their children are now going to school to palefaces on many of the reservations.  When the teachers tell these papooses that there is an ocean, the children take their books to their fathers and ask about it</span>.</p>

		<p>&quot; 'Yes,' replies the red man, 'that is true.  I have seen the Giant Pond myself.'  So, when the child hears of Mount Vesuvius and volcanoes, the old warrior looks at the picture, and tells his child that the pale face is right, for he himself has seen the 'Burning Mountain.'  Col. Cody took his show to Italy in 1892, you know.  It was a strange sight to see the Indian wondering at the sights of this ancient country.  He had great tales to tell his people when he returned home.&quot;</p>

		<p><span title="BBWW: Genuine" style="background-color: transparent;">Most of the Indian acts in the Wild West Show were devised by the redskins themselves.  Their dances are their own absolutely.  Their leaders realize that the purpose of Col. Cody and &quot;Nate&quot; Salsbury is to present illustrative pictures of the real, historic life of these people, and the Indians enter fully into the spirit of the thing</span>.  Major Burke says they never seem to mind being on the losing side every time.</p>

		<p>In the siege of Tien&ndash;Tsin, Indians take the part of Chinese.  They, of course, are beaten back, and are the unpopular ones, but they enjoy it.  In this they differ from the Mexicans, who took the part of Spaniards in the charge on San Juan Hill.  The crowds used to hiss the &quot;Spaniards,&quot; and the men from beyond the Rio Grande didn't relish this at all.  At one time a strike threatened to disturb the calm serenity of Col. Cody's life.</p>

		<p>According to the laws of the United States, all Indians have to stay on the reservations allotted to them.  It is, therefore, <span title="BBWW: Business" style="background-color: transparent;">necessary for Col. Cody to secure permission for the leave of absence of every redskin he employs.  He must give bond, also, for the care of the Indians.  He pays them from the day they leave their reservations until they return; and he pays all their railroad fare besides</span>.</p>

			
