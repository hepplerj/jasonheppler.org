		<h1>LITTLE SIOUX BRAVE'S DEATH.</h1>

		<h3>Jose Bluehorse, Three Years Old, Paraded with Wild West Until He Collapsed.</h3>

		<p><em>Special to The New York Times.</em></p>

		<p>Jose Bluehorse, three years old, a full&ndash;blooded Sioux Indian, who formed one of the features of a &quot;Wild West&quot; show which visited Orange yesterday, died from pneumonia in his parents' tepee last night.  The little fellow had been ill for several days, but with Indian stoicism did not complain, and took part in the street parade of the show yesterday.  He collapsed when he got back to camp.  Dr. E. G. West of Orange was summoned, but he was too late to be of service.</p>

		<p>Assistant Morgue Keeper Markwith had some difficulty with the Indians before he could get the body, so that County Physician McKenzie could ascertain the cause of death, but he succeeded at last when he told them that it would be treated with respect.</p>

		<p>The managers of the show would not let the parents stay behind to superintend the interment of the little fellow, and his cousin, a tall Indian, was delegated to see that the lad was buried properly.  Wrapped in his red blanket, the Indian strode into Markwith's office last night and explained his mission.  On his way to the room where the body lay the Indian plucked feathers from a duster which hung on a wall, and later placed them in the lad's moccasins.  The body was dressed in a miniature war costume, fringed and beaded.  An Indian scarf was draped about the head and a beaded blanket inclosed the little form.</p>

		<p>The Indian took no pains to conceal his sorrow.  He asked if the body would be buried &quot;with prayer,&quot; and Markwith replied not unless some one else were there to do it.  At this the Indian, who was a Roman Catholic, took a crucifix and beads from about his neck and placed them on the child with the crucifix in the little folded hands.  He ten bade the morgue keeper leave the room while he prayed.  Markwith did so and closed the door after him, and in a moment heard the voice of the Indian gravely intoning a chant.  When he had finished that, greatly to Markwith's astonishment he began to intone the Chopin funeral march.  When he finished that Markwith heard him praying.  Then the Indian came out of the room and gave Markwith directions for the child?s burial, laying emphasis on the fact that he would be in town again in the Fall and would call on Markwith if his directions were not carried out to the letter.  He left money to buy a cross to place over the child's grave. The show people paid for a grave in Rosedale Cemetery.</p>

			
