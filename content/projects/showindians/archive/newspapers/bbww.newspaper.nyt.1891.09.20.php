		<h1>DEATH OF A BUFFALO BILL INDIAN.</h1>

		<p><em>From the London Daily News.</em></p>

		<p>Paul &quot;Eagle Star,&quot; one of Col. Cody's Indians, died yesterday at the Sheffield Infirmary, from an accident which befell him while the &quot;Wild West&quot; show was on a visit to that town.  He and a number of other Indians were riding in the arena, when his horse swerved against a part of the boarding.  &quot;Eagle Star&quot; sustained a compound fracture of his leg, one of the fractures being close to the ankle.  For a time he made favorable progress, but lockjaw set in, and from this he died.  Mr. Crager, Col. Cody?s chief interpreter, sat up with him the whole of Sunday night, and he was visited also by &quot;Short Bull,&quot; one of the Indian chiefs.  The deceased was a Brule Sioux Indian, twenty&ndash;five years of age, and was a prisoner of war, having taken part in the last Indian rising.  He was married.  His wife and children are living at the Rosebud Agency, United States. </p>

			
