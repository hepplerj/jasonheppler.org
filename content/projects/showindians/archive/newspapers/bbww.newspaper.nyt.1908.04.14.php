		<h1>BUFFALO BILL'S INDIANS HERE</h1>

		<h3>Come from Rosebud and Pine Ridge for the Wild West Show.</h3>

		<p><span title="Image: Noble Savage" style="background-color: transparent;">One hundred Indians, in war paint and with their war whoops all in working order, arrived yesterday from the Rosebud and Pine Ridge Reservations to join the Buffalo Bill Wild West Show</span>.  They <span title="NA: Travel" style="background-color: transparent;">came in on the Erie, crossed the city to the Grand Central Station, and left for Bridgeport, Conn.</span>, at 6 o'clock.  There they are going to indulge in a few preliminary rehearsals before coming back to New York for the opening performance at the Madison Square Garden on April 21.</p>

		<p>Long before the Indians arrived Col. Cody himself was waiting for them at the gates of the West Twenty&ndash;third Street Ferry.  When the Indians finally came off the ferryboat they crowded around the Colonel.  He greeted many of the Indians by name and singled out old <span title="NA: Members" style="background-color: transparent;">Chief Iron Tail for the singular and especial honor of standing with him in the centre of the group arranged for the camera men.  Among the others in the front row were Acy Ghost&ndash;Dog, Mrs. Ghost&ndash;Dog, and a number of papooses</span>.</p>

			
