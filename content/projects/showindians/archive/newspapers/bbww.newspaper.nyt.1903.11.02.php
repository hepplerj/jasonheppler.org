		<h1>INDIANS IN ENGLAND.</h1>

		<h3>&quot;Buffalo Bill's&quot; Braves, Just Returned, Excited Wonder&mdash;Pappoose Born in Britain.</h3>

		<p><span title="BBWW: Travel" style="background-color: transparent;">&quot;Buffalo Bill,&quot; Major John M. Burke, and fifty of the Indians of the Wild West Show returned to this country Saturday night on the Cunard liner Etruria.  The show has been on tour in England since last Spring, and, with the exception of the returning members, is in Winter quarters, where it will remain until next Spring.  Then a tour of Europe will be made</span>.</p>

		<p>Col. Cody was asked about the report that he was to give up the show business to look after his private interests.</p>

		<p>&quot;I haven't heard any such report,&quot; he said.  &quot;I am not going to give up for a long time.  I have returned to look over some of my property in the West, and to get some more attractions for the show.  We shall all return to Europe in March.</p>

		<p>&quot;I met Mme. Patti on board and exchanged compliments with her.  She is a delightful woman and I should like to hear her.&quot;</p>

		<p>The Indians who came in the second cabin presented an odd appearance on the pier.  Many of them were dressed in their heavy multi&ndash;colored robes.</p>

		<p>One woman carried a small child in her arms, which was the pet of all the passengers on the trip.  <span title="BBWW: Members" style="background-color: transparent;">He is the only Indian child ever born in England.  His name is Alexander Pearl Olive Birmingham England Standing Bear.  He was born in Birmingham on June 7</span>.</p>

		<p>The Indians were very enthusiastic in their praise of the English people.  The Indians were very enthusiastic in their praise of the English people.  They said that while in England they were frequently called into houses as they passed through the streets, and there the people would scrutinize them very closely.</p>

		<p><span title="NA: Descriptions" style="background-color: transparent;">Their long straight black hair was the chief cause for wonder, and people frequently would feel of it, and ask if it were real.  Their robes excited admiration, and one tall lean Indian showed a frock coat and a high hat which he had bought with the proceeds of the sale of his robes</span>.</p>

		<p>The customs officials had no end of difficulty in examining the baggage of the red men.  Their valuables were carried in most unheard&ndash;of places, and there were so many things which they brought back which are seldom seen by the inspectors that a corps of examiners was kept busy for a long time determining what the Indians should pay.</p>

			
