		<h1>COL. CODY GETS HIS INDIANS.</h1>

		<h3>MR. MORGAN FINDS HE IS NOT A BIGGER MAN THAN MR. NOBLE.</h3>

		<p>Indian Commissioner Morgan has discovered once more that he is not a bigger man than the Secretary of the Interior.  <span title="NA: Treatment" style="background-color: transparent;">Somebody started the story last Fall that the Indians who had been taken abroad by Col. William F. Cody to take part in the &quot;Wild West&quot; had been badly treated and were surrounded by degrading influences</span>.</p>

		<p><span title="NA: Treatment" style="background-color: transparent;">As soon as Col. Cody, better known as &quot;Buffalo Bill,&quot; heard of this talk he rounded up his Indians, who were in Winter quarters at Strasburg, and brought them to Washington to give Commissioner Morgan an ocular demonstration of the falsity of the report</span>.  It was an expansive undertaking.  When the Indians reached Washington, Col. Cody sought an interview with Morgan.  To his surprise, the Commissioner kept him waiting from day to day, and it was not until Buffalo Bill had secured the active help of a prominent Cabinet officer that he was able to bring his troop before the Commissioner.  <span title="Context: Indian Wars" style="background-color: transparent;">Then the Indian troubles broke out in the West, and, at the solicitation of the authorities, Cody and his friendly Indians went to Pine Ridge and did good service for the Government</span>.</p>

		<p>But when the Colonel got ready to take his Indians back to Europe, he found a large&ndash;sized snag in his way.  <span title="BBWW: Immoral" style="background-color: transparent;"><span title="Progressivism: Indian Welfare" style="background-color: transparent;"><span title="Image: Immoral" style="background-color: transparent;">Commissioner Morgan flatly refused to permit a single red man to leave the reservation.  He had been told that the Indians did not have proper surroundings abroad, and he considered it much better that they should remain on the reservation than be demoralized by foreign travel</span></span></span>.</p>

		<p>No amount of evidence that Col. Cody could produce, included that of many leading Americans who had seen the show in Europe, could affect Commissioner Morgan, nor would he listen to the <span title="Context: Indian Welfare" style="background-color: transparent;">recommendations of Indian agents and Gen. Miles and Col. Forsythe that it would be the best way to prevent a renewal of troubles in the Spring to let Cody take a hundred of the Sioux out of the country</span>. The Nebraska Senators and Representatives joined in trying to induce Morgan to issue the permits, but the only result was to call out an order from Morgan for the arrest of any agent of Cody who tried to take an Indian away from the reservation.</p>

		<p>It had cost Col. Cody a good many thousand dollars to bring his hundred Indians to the United States and take care of them here, and then to have them suddenly corralled by the Indian Commissioner after their return passage had been paid for was a little disheartening.  Col. Cody has been here a week working hard to prevent the destruction of his show by Morgan's arbitrary act.  The matter was finally laid before Secretary Noble, and to&ndash;day the Secretary overruled the Commissioner, and issued an order directing that Col. Cody be given liberty to take to Europe as many of the Sioux Indians as he wished.  A fortnight hence 100 of the redskins will sail with Buffalo Bill, and <span title="BBWW: Immoral" style="background-color: transparent;"><span title="Image: Immoral" style="background-color: transparent;">Commissioner Morgan, who boasted that he never attended a theatre or a circus in his life, will have to give them up to the demoralizing and degrading influences of foreign travel and contact with the civilization of the white man</span></span>.</p>

			
