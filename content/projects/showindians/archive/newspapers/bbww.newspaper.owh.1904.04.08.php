		<h1>SIOUX INDIANS DIE IN THE RAILROAD WRECK</h1>

		<h3>Three Killed, Three Fatally Injured and Nine Hurt While Going to Join Buffalo Bill.</h3>

		<h3>Chief White Horse, Body Crushed and Both Legs Broken, Calmly Smokes His Pipe.</h3>

		<p>In a rear end collision between two Northwestern trains between Melrose Park and Maywood this morning, a coach containing a company of Sioux Indians, on their way from the Pine Ridge agency to join Buffalo Bill's show in England, was smashed.</p>

		<p><span title="NA: Treatment" style="background-color: transparent;">Three Indians were killed, three fatally injured and nine more or less seriously hurt</span>.</p>

		<p>Among the injured are Chief High Bear and Chief White Horse.  The latter will die.</p>

		<p>The collision occurred in a dense fog.</p>

		<p>Dead: <br />

		<span title="NA: Members" style="background-color: transparent;">KILLED HEAD</span>, skull crushed</span><br />

		<span title="NA: Members" style="background-color: transparent;">PHILIP IRONTAIL JR.</span>, body crushed<br />

		<span title="NA: Members" style="background-color: transparent;">THOMAS COME LAST</span>, body crushed</p>

		<p>The injured,<br />

		<span title="NA: Members" style="background-color: transparent;">Big Chief White Horse</span>, body crushed and legs broken, will die<br />

		<span title="NA: Members" style="background-color: transparent;">Luther Standing Bear</span>, crushed about body and head, will die<br />

		<span title="NA: Members" style="background-color: transparent;">Annie Gooseface</span>, crushed about body; will die<br />

		<span title="NA: Members" style="background-color: transparent;">Mrs. Thomas Come Last</span>, wife of one of the killed, cut about the head and shoulders.<br />

		<span title="NA: Members" style="background-color: transparent;">Tommie Come Last</span>, 5 years old, son of the dead, head bumped and believed to have been made temporarily insane from fright.<br />

		<span title="NA: Members" style="background-color: transparent;">Sammy Lone Bear</span>, head cut and body bruised.<br />

		<span title="NA: Members" style="background-color: transparent;">Abraham Good Crow</span>, cut about head.<br />

		<span title="NA: Members" style="background-color: transparent;">Blind Eagle</span>, cut about the head by flying glass.<br />

		<span title="NA: Members" style="background-color: transparent;">Little Elk</span>, head cut and bruised.<br />

		<span title="NA: Members" style="background-color: transparent;">Charge the Enemy</span>, head cut and body bruised.<br />

		<span title="NA: Members" style="background-color: transparent;">Head Chief High Bear</span>, at the head of the delegation of Sioux, severely crushed about the body<br />

		<span title="NA: Members" style="background-color: transparent;">William Sitting Bull</span>, son of Chief Sitting Bull, 27 years old, seriously injured</p>

		<h3>REAR END COLLISION</h3>

		<p>The trains which collided were the Oregon express and the fast mail.  The express train was run into by the mail train.  Both trains were east bound.</p>

		<p>Physicians were hurried to the scene.  As soon as possible the injured were taken to Maywood and Chicago.</p>

		<p>All the victims of the wreck were Indians.</p>

		<p>The coach containing the Indians was a light day car.  It was completely wrecked.  The rear end of the coach ahead was also damaged.</p>

		<p>After the collision the passengers in the other coaches of the two trains hurried to the rescue, and after a hard struggle pulled the Indians from beneath the wreckage.</p>

		<p>Chief White Horse, in charge of the Indians, was fatally injured.</p>

		<p>The bodies of the Indians who had been killed outright were laid on the prairie beside the track, Chief White Horse being carried with them.  He said he knew that death was near, and requested that he be placed near his dead companions.</p>

		<h3>SMOKES PIP DYING.</h3>

		<p>The chief was propped up and sat stoically while physicians worked over his injuries.</p>

		<p>He smoked a pipe quietly and showed no signs of the pain he must have been suffering.</p>

		<p>One by one the other injured Indians were slowly taken from the splintered car and placed on the ground near their dying chief and dead comrades.</p>

		<p>The physicians hurried from one to another of the injured, administering to the wants of all, while arrangements were being made to take them to the nearest hospital.</p>

		<p>The Indians were from the reservation near Rushville, Neb., and their journey east was primarily for show purposes at New York.</p>

		<p>Persons who were on the train said that the collision unquestionably was the result of the dense fog which today stretched from Lake Michigan many miles westward.  In addition to the fog, clouds of escaping steam enveloped the wreck and added to the difficulty of rescue.</p>

		<h3>YELLS OF THE RED MEN.</h3>

		<p>A moment after the crash there was not a sound from the ill-fated coach, then one by one those surviving the crash regained in part their senses and began shouting for aid.  A terrifying series of wild yells from the Indians were heard by the passengers, jumping from each side of the cars ahead.  All the men passengers and many of the women on board the two trains hastened to the aid of the injured Indians.  The fact that one side of the rear car fell off with the impact undoubtedly saved the lives of many Indians who were thrown out on the prairie.</p>

		<p>It was a pitable sight to see the Indians lying on ground clad in their native garments with their red blankets dyed deeper by the blood of their friends.  The men were of splendid physique, and the exhibition of calm nerve on the part of White Horse was a good example of that of the other members of the Indian party. </p> 

		<p><span title="NA: Descriptions" style="background-color: transparent;">After the bodies of the dead had been removed from the wreckage and placed in a row on the prairie beside the tracks an incident probably unparalleled in railway records took place.  The uninjured among the Indians gathered about and led by Chief Iron Tail solemnly chanted the Indian &quot;death song.&quot;</span></p>

