		<h1>LO'S PLACE IS AT HOME.</h1>

		<h3>&quot;Wild West&quot; Shows Denounced by the Congregational Club.</h3>

		<h3>THEY DEMORALIZE INDIANS.</h3>

		<h3>So Says Miss Mary C. Collins, a Reservation Missionary.</h3>

		<h3>COMMISSIONER MORGAN'S ADDRESS</h3>

		<h3>The Red Men Should Be Taught to Go Where Work Is, He Says.</h3>

		<h3>RESOLUTIONS ADOPTED AT THE BANQUET.</h3>

		<p>Buffalo Bill and his &quot;Wild West&quot; show were handled without gloves at the banquet of the Congregational Club at the Grand Pacific Hotel last night, and there is a probability that he may find it difficult to obtain the Indians he wants this season.</p>

		<p><span title="Progressivism: Indian Problem" style="background-color: transparent;">The topic for discussion was the &quot;Indian Problem.&quot;  Dr. Charles A. Eastman, an educated Sioux; Gen. T. J. Morgan, United States Indian Commissioner; and Miss Mary C. Collins were the speakers of the evening</span>.</p>

		<p><span title="BBWW: Immoral" style="background-color: transparent;">Miss Collins was strongly opposed to the Government officials permitting showmen to cart Indians around this country and Europe like wild beasts</span>.  She was generously applauded and as a result a set of resolutions was adopted condemning the practice and authorizing the Chairman to appoint Miss Collins and two members of the club as a committee to visit President Harrison and ask his official interference.</p>

		<p>N. H. Whittlesey presided at the banquet and introduced the first speaker, Dr. Eastman.  His Indian name is &quot;Ohiyesa,&quot; meaning winter.  He was educated in the State Normal School of Nebraska.  From there he went to Knox College, Galesburg, then to Beloit, Wis., then to Dartmouth College, and finally graduated from the medical department of the Boston University.</p>

		<p>He said he had been asked if he intended to settle in the East and build up a practice.  To this he replied:</p>

		<p>&quot;I did not get my education to make money, but to help my people.&quot;</p>

		<p>In the course of his remarks he said:</p>

		<h3>The Indian Problem.</h3>

		<p>&quot;<span title="Progressivism: Indian Welfare" style="background-color: transparent;">The Government has greatly improved in the management of the Indians.  The Government schools are good and are supplied with good, intelligent teachers</span>.  But there is a great deal to be accomplished yet.  The Indians cannot always live on reservations.  <span title="Image: Civilization" style="background-color: transparent;">The rapid progress of civilization will do away with this mode of treatment before many years</span>.</p>

		<p>&quot;<span title="Context: Indian Wars" style="background-color: transparent;">Since the late outbreak in the Northwest there has been much talk about the young men and women who were educated at the Government schools having joined the rebels</span>.  Well, this is not surprising to me.  If you were to send me back to the reservation, and make me stay there with nothing to do, I think I would go back to the old form of life.  I would take up my blanket like my ancestors.  <span title="Progressivism: Indian Welfare" style="background-color: transparent;">What is wanted for the graduates from the Indian schools is work, not money</span>.</p>

		<p>&quot;If they don't get work they naturally sink back to their old manner of living.  The young Indians are taught to make shoes, to do carpenter work and tailoring, and then they are sent back to the reservation, where they have little, if any, chance to put their knowledge into practical use.  The Indians are not as ignorant as a class as many of the white people think.  They have strong prejudices and often are quick to act.  They&mdash;that is the older Indians&mdash;think the white people make promises with no intention of keeping them.  They believe the whites are only trying to get rid of the Indians in order to get the lands.</p>

		<p>&quot;Several Indians came to me during the ghost dances and said that after all our God is the same as the white man?s God.  They said the Episcopalians, Baptists, Presbyterians, and Catholics all have their religions and each claim to be right and we have our way and claim we are right.  The Indians are sharp and quick to find flaws in the religions and the missionaries cannot be too careful in explaining the Bible.&quot;</p>

		<p>When he applause had eased the Chairman introduced Gen. Morgan.</p>

			
