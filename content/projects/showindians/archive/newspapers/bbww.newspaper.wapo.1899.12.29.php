		<h1>BUFFALO BILL ENTERS PROTEST</h1>

		<h3>Commissioner Jones' Indian Order Would Ruin Wild West Show.</h3>

		<p>Since the recent announcement of the new policy of the Interior Department discountenancing wild Indian exhibitions, the department has been deluged with both written and oral inquiries. <span title="BBWW: Business" style="background-color: transparent;">Col. W. F. Cody has protested that the action will nearly ruin his business</span>.  He sent a representative here who urged Commissioner of Indian Affairs Jones to make an exception in his case.  A number of other interested parties also have called at the Indian Bureau on the subject.  A large number of letters on the subject, mostly indorsing [sic] the department's attitude, have come by mail.</p>

		<p>Commissioner Jones said there would be no change in the department's policy, and that he was <span title="Progressivism: Reform" style="background-color: transparent;">convinced the exhibitions have a demoralizing tendency and retard Indian progress.  It is recognized, however, that, failing the government consent, Indians may be contracted with and withdrawn from the reservations and exhibited without the present formality of securing official sanction</span>.</p>

