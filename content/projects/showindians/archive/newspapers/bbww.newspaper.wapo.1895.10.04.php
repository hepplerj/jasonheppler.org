		<h1>Indians Complain to the Commissioner.</h1>

		<p>Col. William F. Cody was at the Indian Office yesterday with a large delegation of Indians connected with his wild West show.  They were given a hearing by Acting Commissioner Smith, to whom they complained that their treaties were not being carried out according to agreement, one of them saying that if he should stop to tell all that the government had promised them it would occupy him two days.  He said they were short of rations, clothing, and blankets on the reservations.  All those that spoke said they were being treated well by Col. Cody.</p>

