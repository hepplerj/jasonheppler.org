		<h1>NO MORE INDIANS FOR WILD WEST SHOWS.</h1>

		<h3>Redskins Can No Longer Be Taken from Their Reservations.</h3>

		<p><span title="BBWW: Travel" style="background-color: transparent;">If Secretary of the Interior Noble's instructions to the Commissioner of Indian Affairs are carried out there is an end of Wild West shows, so far as Indians are concerned, both in this country and in Europe</span>.  Gen. Oberiene wrote to Secretary Noble a few days ago notifying him that a syndicate had been formed to get another party of Indians from the reservations in order to keep up the Wild West shows in Europe, also that an agent of the Wild West was on the way to get them.  Secretary Noble sent this reply to Gen. Oberiene today:</p>

		<p>Your favor of the 26th inst. has been received and I have referred it to the Commissioner of Indian Affairs, with <span title="BBWW: Travel" style="background-color: transparent;">instructions to prohibit and prevent any Indians from being taken from the reservations for the purposes mentioned</span>.</p>

			
