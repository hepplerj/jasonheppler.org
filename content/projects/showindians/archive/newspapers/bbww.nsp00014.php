		<h2>INDIANS GUESTS OF HONOR.</h2>

		<h3>Mrs. Henry Bruton Entertains Three from the Wild West Show.</h3>

		<p>Indians in the somewhat incongruous surroundings of beautiful furniture, fine paintings, cut glass, silver, and charming women in beautiful gowns were a conspicuous feature at a breakfast given by Mrs. Henry Bruton at the Hotel Manhattan yesterday morning. There were present three Indians from the Wild West Show, in their most elaborate attire, feather headdresses, bead jackets, beaded blankets, and beautiful armlets. The breakfast was given in their honor. Mrs. Bruton has traveled extensively in Mexico and the West, and feels a peculiar interest in the red men, whom she has seen on their native soil.</p>

		<p>The breakfast was at 11 o'clock to suit the convenience of the guests. The table was handsomely decorated.  there were quantities of flowers, and a bouquet for each Indian, besides trinkets of various kinds. There were some twenty other guests present. The guests of honor were Mr. Sammy Lone Bear, Mr. Joe Black Fox, and Mr. Philip Standing Soldier. It may be said that the manners of the three were unexceptional. They ate with their forks at the table and carried themselves during the two hours' of their stay with dignified freedom.<p>

		<p>Mrs. Bruton bought all the Indian trinkets the guests of honor had with them, to send to friends abroad.  A photographer was also present and took photographs of Mrs. Bruton and her guests, which will also go abroad.</p>

			
