		<h1>SHOW INDIANS BADLY ABUSED</h1>

		<h3>Braves With the Carver-Whitney Troupe Come Home Penniless.</h3>

		<h3>Shot at Repeatedly and Imprisoned in Germany.</h3>

		<h3>Chiefs Who Arrive In New York Tell How They Were Robbed and Shamefully Ill-Treated, and Their Stories Corroborated.</h3>

		<p>Two of the Indian braves who have been in Europe with the Carver-Whitney show were steerage passengers on the Hamburg steamer Augusta Victoria, which arrived yesterday.  They answered to the names of <span title="NA: Members" style="background-color: transparent;">&quot;Sunk Owa,&quot; or &quot;Painted Horse,&quot; who is a nephew of Red Cloud; and &quot;Wambali-Pa,&quot; or &quot;Eagle Head.&quot;</span>  They were full-blooded Sioux of the Ogallala band, and they came over with James Marsh, who was advertising agent for the show on the continent.</p>

		<p>Both braves were without a penny, and their entire possessions consisted of the rough shirts they wore and a railroad ticket each to Pine Ridge Agency, Dakota.  &quot;Painted Horse&quot; said they were shamefully treated in the Carver-Whitney show, and gave an account of the ill-treatment to General O'Beirne at the Barge Office, through Rev. Father Crafts, who acted as interpreter.  Substantially &quot;Painted Horse&quot; said:</p>

		<h3>THE STORY OF ILL-USAGE.</h3>

		<p>There were twenty of us when we left New York besides Black Bear's squaw and their three children, and <span title="NA: Treatment" style="background-color: transparent;">during our travels over Europe we were beaten, kicked and shot at.  Dr. Carver shot at Black Bear, but failed to hit him, though he intended to kill him</span>.  I tried with four others to communicate with some of Black Bear's relatives in the Buffalo Bill Show in Hamburg.  We were locked up in a German prison by the German police who were sent after us because I objected to being beaten and ill-used.&quot;</p>

		<p><span title="NA: Treatment" style="background-color: transparent;">&quot;Dr. Carver fired at me with a revolver in Berlin fifteen days ago, but missed me.  After we were locked up by the German police Carver took all our money away, about $25 each, and did not return it.  Our money was not paid regularly, and the whites in both shows tried to corrupt the red men.  The Indians got liquor whenever they desired</span>.  Some of Carver's and Cody's men had a row in Hamburg, but the Indians were not in it.&quot;</p>

		<h3>WHITES SUFFER ALSO.</h3>

		<p><span title="NA: Treatment" style="background-color: transparent;">&quot;Three braves were in a dying condition when we left--Bear Necklace, Brave Elk and another.  The squaw was also ill and the papoose died as we were leaving.  We received only five dollars besides our tickets, and now we have nothing.  I was in Buffalo Bill's show before I went to Carver's.  I saw him knock down two braves.  Both died on their return to Pine Ridge.  The food Buffalo Bill supplied was bad and the braves who ate it were sick, Buffalo Bill was brutal.&quot;</span></p>

		<p>Agent Marsh corroborated all that Painted Horse had said.  Both Cody and Whitney, he said, had left white men stranded in various parts of Europe; some are now in Harrisburg penniless.  Three young Americans were left without a cent.  Others from London and Paris had no means to get home.  The American Consuls at the various ports were helpless.  Men who had been employed in the shows had been reduced to the necessity of begging bread.</p>

		<p>General Oberine will go to Philadelphia as soon as the remainder of Buffalo Bill's Indians arrive there on the Belgenland.  He will assist Herbert Walsh, secretary of the Indian Rights Association, in investigating treatment of the braves.</p>

