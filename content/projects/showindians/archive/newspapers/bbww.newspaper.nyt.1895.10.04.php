		<h1>INDIANS MAKE THEIR COMPLAINTS</h1>

		<h3>Members of the Wild West Show Visit Commissioner Smith.</h3>

		<p><span title="NA: Descriptions" style="background-color: transparent;">There was a picturesque scene in the office of Assistant Indian Commissioner Smith to&ndash;day, when about seventy&ndash;five Ogallalla and Brule Sioux of Buffalo Bill's Wild West Show called to have a powwow in regard to affairs affecting their interests</span>.  The Indians were <span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Noble Savage" style="background-color: transparent;">fine specimens of physical manhood, and were bedecked with feathered headdress, and their features were decorated with variegated colors of paint</span></span>.  There were three squaws and one papoose among them, the latter occupying the attention of the lady clerks, who fed it sugar and treated the little redskin to a ride on the elevator, much to its delight.</p>

		<p>Several chiefs addressed Commissioner Smith through an interpreter, reviewing their affairs, and <span title="NA: Treatment" style="background-color: transparent;">one old warrior, who spoke at some length, stated that if he told of the unfulfilled promises of the Government to the Indians, the show would have to be postponed, as it would take him two days to do it</span>.  One of the principal complaints of the Indians was the reduction in the price paid to them for hauling freight.</p>

		<p>Commissioner Smith replied to the speakers and explained the action of the Government in regard to their affairs.  Col. Cody and Major Burke accompanied the Indians.</p>

			
