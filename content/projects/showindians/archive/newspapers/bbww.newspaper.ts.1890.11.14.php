		<h1>ARRIVAL OF BUFFALO BILL'S INDIANS.</h1>

		<p>The Red Star steamer Belgenland arrived at her dock today, bringing back 39 Indians that have been in Europe with Buffalo Bill's show.  Herbert Welsh, secretary of the Indian Rights' Association, was present to receive them, and to <span title="NA: Treatment" style="background-color: transparent;">investigate by authority of the government the charge that they, though wards of the nation, were abused by the managers of the show</span>.  General O'Beirne, of New York, accompanied him, and will also report to the Interior Department at Washington.  <span title="NA: Treatment" style="background-color: transparent;">Major John M. Burke had charge of the Indians.  He denied emphatically the charges of ill&ndash;treatment, and wanted an examination of the Indians made in public, but to this Mr. Welsh objected</span>.  <span title="Progressivism: Indian Welfare" style="background-color: transparent;">The Indians look to be well fed and contended, but few of them can talk more than a word or two of English, and hence cannot be interviewed.  Major Burke went to Washington, where he will have a hearing tomorrow before the Secretary of the Interior regarding the charges</span>.</p>

