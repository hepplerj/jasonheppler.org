		<h1>EASY TO CARE FOR 100 INDIANS.</h1>

		<h3>William McCune, With the Buffalo Bill Show, Tells How They Are Controlled.</h3>

		<p>&quot;<span title="Progressivism: Indian Welfare" style="background-color: transparent;">One hundred Sioux Indians, fresh from the reservation, are much easier to control than as many white men, under similar circumstances</span>.  <span title="NA: Treatment" style="background-color: transparent;">Last season we made a remarkable record.  Not a single Indian had to be returned to the reservation because of misconduct</span>.&quot;</p>

		<p>The speaker was William McCune.  He is the general Western agent for Buffalo Bill's Wild West show.  Mr. McCune is spending a few days in Kansas City at the home of his sister, Mrs. J. S. Scott, who lives at 2409 Indiana avenue.</p>

		<p>Mr. McCune has just returned from a special mission on which he goes twice a year for Colonel William F. Cody, the owner of the show.  In the early spring he goes to the <span title="BBWW: Business" style="background-color: transparent;">Pine Ridge Indian agency in South Dakota.  There he selects 100 Sioux warriors, squaws, and pappooses to accompany the Wild West show during the season</span>.  In the fall or early winter Mr. McCune travels westward again, accompanying the Indians back to the reservation.  Mr. McCune speaks the Sioux tongue readily.</p>

		<p>&quot;It's easy to control the Indians, if you understand them,&quot; continued Mr. McCune.  &quot;We appoint a chief and six police from among the Indians who go with the show.  <span title="Image: Immoral" style="background-color: transparent;">The Indians who get drunk or become disturbers are fined $5.  The money comes out of their pay.  If the offense is repeated they are fined $10.  The Indian police report the cases to the managers of the show through the chief.  At the end of the season the fine money is divided equally among the chief and the police</span>.  Last season we didn't raise a cent.</p>

		<p>&quot;<span title="BBWW: Business" style="background-color: transparent;">Buffalo Bill is preparing to start in the spring on a four year's tour of America.  His show is traveling with Bailey's circus.  This is to be his farewell tour.  He is to retire from the show business after the tour has been completed</span>.  The show has just returned from a very successful tour of England and the continent.&quot;</p>

			
