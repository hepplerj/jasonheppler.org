		<h2>BUFFALO BILL ENTERS PROTEST.</h2>

		<h4>Commissioner Jones' Indian Order Would Ruin Wild West Show.</h4>

		<p>Since the recent announcement of the new policy of the Interior Department discountenancing wild Indian exhibitions, the department has been deluged with both written and oral inquiries.  Col. W. F. Cody has protested that the action will nearly ruin his business.  He sent a representative here who urged Commissioner of Indian Affairs Jones to make an exception in his case.  A number of other interested parties also have called at the Indian Bureau on the subject.  A large number of letters on the subject, mostly indorsing the department's attitude, have come by mail.</p>

		<p>Commissioner Jones said there would be no change in the department's policy, and that he was convinced the exhibitions have a demoralizing tendency and retard Indian progress.  It is recognized, however, that, failing the government consent, Indians may be contracted with and widhrawn from the reservations and exhibited without the present formality of securing official sanction.</p>

