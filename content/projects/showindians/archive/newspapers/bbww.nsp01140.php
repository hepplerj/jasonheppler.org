		<h1>THE INDIAN ACTORS.</h1>

		<h3>An Attempt by the Interior Department to take them from Buffalo Bill.</h3>

		<p><span title="Progressivism: Indian Welfare" style="background-color: transparent;">The commissioner of Indian affairs, having found that the Pawnees with the Buffalo Bill combination are off their reservation without permission, issued an order that they should return immediately</span>.  <span title="NA: Descriptions" style="background-color: transparent;">When this fact became known to the Indians they executed a terrific war dance, held an excited pow-wow, and finally concluded not to go</span>.  Mr. Cody then took the matter in hand, and called upon the commissioner, stating that <span title="BBWW: Business" style="background-color: transparent;">if the order was carried into effect, it would cause considerable pecuniary loss to him, as the combination had been advertised for appearance for twenty days ahead</span>.  <span title="NA: Treatment" style="background-color: transparent;">He also assured the commissioner that the Indians were well treated</span>.  <span title="Progressivism: Indian Welfare" style="background-color: transparent;">The commissioner then revoked his order, on condition that Mr. Cody enter into $5,000 bonds to return the Indians after a specified time (within which it is agreed the Indians shall be kept sober) to their agency at his own expense</span>.  This arrangement was satisfactory, and Mr. Cody departed happy.  The dusky chiefs of the forest sang &quot;O&ndash;be&ndash;joyful!&quot; and will <span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Savagery" style="background-color: transparent;">indulge in deeds of daring and blood-curdling horror upon the stage, instead of living on Government rations at their agency</span></span>.</p>

