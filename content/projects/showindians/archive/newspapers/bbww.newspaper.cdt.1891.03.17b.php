		<h1>MISS COLLINS ATTACKS COL. CODY.</h1>

		<h3>Her Arraignment of &quot;Wild West&quot; Shows&mdash;Resolutions Adopted by the Club.</h3>

		<p>Miss Mary C. Collins, for sixteen years a Congregational missionary among the Sioux Indians, followed Gen. Morgan and devoted much of her time to telling of the &quot;great wrong&quot; that was being done to the Indians by the Government officials, who allowed men to cart them about the country with shows.  In the course of her remarks she said:</p>

		<p>&quot;<span title="Progressivism: Indian Welfare" style="background-color: transparent;">I consider it a privilege to speak to an audience which I know can help the Indians.  I almost forget at times that I am not an Indian.  I have been with them so long and have been interested in their welfare so much</span>.  I could scarcely keep back the tears when I sat there and heard Dr. Eastman speak.  I remember so well when he was a little boy and started out in life under the care of our mission.  I remember also his father, who was a chief, and <span title="Progressivism: Religious Reform" style="background-color: transparent;">though he could not speak a word of English he was a Christian</span>.</p>

		<p>&quot;<span title="BBWW: Immoral" style="background-color: transparent;"><span title="Image: Immoral" style="background-color: transparent;">When Sitting Bull returned from Canada and was sent out as a curiosity with showmen and then came back to his people he had the big head and would take no part with the progressive Indians</span></span>.  <span title="BBWW: Immoral" style="background-color: transparent;">It was largely due, I think, to the influences which were brought to bear on him while he was on exhibition</span>.  From what he told me and from what I heard him tell the Indians concerning his travels it is not at all surprising that he returned to his native mode of life.</p>

		<p>&quot;It was not only so in Sitting Bull's case but <span title="BBWW: Immoral" style="background-color: transparent;"><span title="Image: Immoral" style="background-color: transparent;">in nearly every instance where Indians have been sent out with shows.  I have noticed that they usually come back morally degraded</span></span>.</p>

		<p>&quot;I understand that Buffalo Bill has arranged to take a band of the prisoners out here at Fort Sheridan around with his show this season.  <span title="Progressivism: Reform" style="background-color: transparent;">It is an outrage to our Christian civilization.  If they are guilty, let them be punished, and if not, send them back to the reservation</span>.  <span title="Progressivism: Morality" style="background-color: transparent;">I appeal to you gentlemen here tonight whether you will let them be sent out as curiosities.  These Indians are not under the control of the Department of the Interior, but the War Department, which says it is the only concern which shows how to deal properly with Indians</span>.&quot;</p>

		<p>Continuing, she told how she had gone out to Fort Sheridan, and how the Indians had said they would be hung when they were sent back to the reservation unless they went with Buffalo Bill to Europe.  And the authorities at the fort allowed the prisoners to be taken out to neighboring towns and put on exhibition.  While she was holding services in a tent at the fort Sunday a man came and broke up the meeting in order that the Indians might be taken out to be exhibited.</p>

		<h3>Resolution Adopted.</h3>

		<p>When she finished her talk Gen. C. H. Howard presented the following resolutions and moved their adoption:</p>

		<p><em>WHEREAS</em>, It is currently reported that the Executive Department of the United States Government has given permission to William F. Cody, known as &quot;Buffalo Bill,&quot; to take the Indian prisoners now at Fort Sheridan and make them a part of his traveling show in this country and in Europe; and</p>

		<p><em>WHEREAS</em>, Such treatment of these prisoners of war is a travesty of justice, and would result in demoralizing to the whole Indian people as far as known, and particularly to the Dakota tribes to which they are related; and</p>

		<p><em>WHEREAS</em>, This treatment of these or any Indians is utterly opposed to the judgment of our missionaries, who are laboring for this race, and is repugnant to the higher instincts of the Christian people of the land.  Therefore be it</p>

		<p><em>Resolved</em>, That it is the sense of the Congregational Club that the order granting this permission should be countermanded and our country saved this disgrace.</p>

		<p><em>Resolved</em>, That a committee of three, including Miss Collins, be appointed to communicate these resolutions to the President of the United States and request his official action in the matter.</p>

		<p>The resolutions were unanimously adopted and the meeting adjourned.</p>

		<p>R. S. Haslan, &quot;Pony Bob,&quot; said last night regarding Miss Collins' charges:</p>

		<p>&quot;Col. Cody is at North Platte, Neb.  There is not the slightest truth in the statement that the Indians were told they would be hung unless they went to Europe with Buffalo Bill.  There were officers present at the interviews and so were the interpreters.  Besides, two of the Indians at Fort Sheridan have been to Europe before with Buffalo Bill and urged the others to accept the offer.  Twenty out of the twenty&ndash;seven will go.  They will get $25 a month, and all expenses.&quot;</p>

			
