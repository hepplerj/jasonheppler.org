		<h1>NO MORE WILD WEST SHOWS.</h1>

		<h3>The Government Will Not Permit Indians to be Used in That Way</h3>

		<p>A new departure in the policy of the Department of the Interior is emphasized by <span title="Progressivism: Indian Welfare" style="background-color: transparent;">unequivocal refusals which have met all recent requests for loans of Indians for exhibition purposes</span>.  Secretary Hitchock and Commissioner of Indian Affairs Jones have determined to stop the abuses of the privileges, and have so stated in a number of communications lately.</p>

		<p>Mr. Jones said to&ndash;day: &quot;There will be <span title="BBWW: Immoral" style="background-color: transparent;">no more Indians permitted to leave their reservations for Wild West Exhibitions purposes.  The consent of the Indian Bureau for such exhibitions cannot be secured unless they are planned on lines showing the progress in Indian education.  The day when the departments permitted Indians to be let for Wild West shows and such affairs is past.  <span title="Progressivism: Reform" style="background-color: transparent;">Such shows demoralize the Indians, many of whom would dress in citizen's clothes and would otherwise conform to more civilized ways but for the money there is in continuing in their old customs</span></span>.&quot;</p>

			
