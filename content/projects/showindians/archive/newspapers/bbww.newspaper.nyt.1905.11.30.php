		<h1>IRON HEART'S BAND BACK.</h1>

		<h3>Buffalo Bill Indians Return After a Few Months in France.</h3>

		<p>The most conspicuous persons on board the Fuerst Bismarck when she reached this port yesterday were <span title="NA: Members" style="background-color: transparent;">forty&ndash;eight Sioux Indians</span>, including two squaws and and several little braves.  <span title="NA: Travel" style="background-color: transparent;">They have been with the Buffalo Bill's show in France, particularly in Paris</span>.</p>

		<p><span title="NA: Members" style="background-color: transparent;">The Indians are bound for the Pine Ridge Reservation in South Dakota</span>. The Government does not allow them to be away from the reservation for an entire year, and so Buffalo Bill, according to contract, had to ship them back.  They left the show at Marseilles.</p>

		<p>The aggregation had such names as <span title="NA: Members" style="background-color: transparent;">Iron Heart, Chief of the band; Wounded Horse and Mrs. Wounded Horse, Jerome Wolfheart, White Rabbit, Parts&ndash;His&ndash;Hair, Ghost, Bear, Dull Knife, Red Shirt, Little Dog, Frank Red Fish, &amp;c</span>.  Several of them spoke English fairly well, particularly Chief Iron Heart.  He as well as the rest of the band admit that they were seasick on board.</p>

		<p>There was some humor in the formalities which now require incoming passengers to be inspected.  Immigration Inspector Dinkel facetiously asking Iron Heart:</p>

		<p>&quot;Are you an American citizen?&quot;</p>

		<p>&quot;Ugh, ugh,&quot; replied the Chief&mdash;he didn't understand.  When the lought about him had abated, and they told him what the question was, he said he thought he was.</p>

		<p>The band was in charge of William Francis McCune, who is to take the Indians back to the reservation.</p>

			
