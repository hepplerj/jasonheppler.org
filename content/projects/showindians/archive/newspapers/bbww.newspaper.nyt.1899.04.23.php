		<h1>THE INDIAN AS A GENTLEMAN.</h1>

		<h3>Those of the Wild West Are Most Chivalrously Inclined, as Shown at a Reception and &quot;Show&quot; Party.</h3>

		<p><span title="Image: Civilization" style="background-color: transparent;">If there is any one who cherishes an idea that a Wild West Indian is not a gallant and chivalrous gentleman such an iconoclast should have been at the Wild West Show last Wednesday night</span>.  <span title="NA: Descriptions" style="background-color: transparent;">To have seen <span title="NA: Members" style="background-color: transparent;">Iron Tail</span>, the Chief, ride past a certain place in the big arena, his face wreathed with smiles, waving the long feathered pole he carried in his hand to a party in the seats, and to have seen <span title="NA: Members" style="background-color: transparent;">Joe Black Fox's</span> shining countenance beaming above a big bunch of spangled violets would dispel all doubts</span>.</p>

		<p>Mrs. Gertrude K&auml;sebier, the artist photographer, was chaperoning a theatre, or more properly a &quot;show&quot; party which had come for the sole and only purpose of seeing the Indians.  It was to this party that Iron Tail was waving his insignia of office, and the spangled violets that set off Joe Black Fox's rosy complexion to such advantage were the gift of one of his admiring white friends.</p>

		<p><span title="Progressivism: Nostalgia" style="background-color: transparent;">Mrs. K&auml;sebier has had a great admiration for all Indians since the time, a good many years ago, when, with her mother, she crossed the plains in the big plains wagons at the time when all Indians were good Indians who loved the whites.  They used to borrow the little white girl, who was not much more than a baby, take her to play with the Indian papooses and spend the day and then sent her back to her mother with her apron filled with buffalo meat</span>.  That was a time when there were many buffaloes as well as good Indians on the plains.</p>

		<p>Last year Mrs. K&auml;sebier invited the Indians to her studio to an afternoon tea, and to be perpetuated for ythe benefit of posterity.  <span title="Image: Civilization" style="background-color: transparent;">Her guests were so much pleased with their reception that they spent the greater part of the time after it as long as the Wild West was in town in making party calls and becoming acquainted with Mrs. K&auml;sebier's various friends.  When they left they did not forget, but sent back letters from time to time to inform their friends of their whereabouts and incidentally to remark that if there were any silk handkerchiefs in New York for which there was no demand they would find them very acceptable</span>.</p>

		<p>That was quite according to Hoyle, for they had been asked to make their wants known, and they had presented their hospitable friends with some beautifully beaded war clubs and other Indian treasures in return for civilities received.  So when the Indians returned to New York again there was another invitation to a studio party, to which a number of people were invited to help entertain them.  It was an all-day reception.</p>

		<p>The invitation had said come and take a studio luncheon at noon, but the authorities that be, for the Indians, had found that this would not be possible, for they were some of them to go to Grant's Tomb and some of them to Liberty Island&mdash;as a treat for the Indians very likely, and also just possibly for certain business reasons of the management.  But if the management thought that its charges were to be invited to luncheon and then make a morning call and go away without anything to eat, it reckoned without its Indians.  They went to make their morning call, they were introduced to all the people who had been invited to meet them, and then they stayed and stayed.  Eleven o'clock came, the time for them to depart, and still they were there, and it was 1 o'clock before they finally left, and then they had had their luncheon, and such a good one that they came back again in the afternoon to make an early party call.</p>

		<p>They hadn't forgotten their friends during the year, or the silk handkerchiefs, for they had brought them back moccasins, some nice little belts, and Sammy Lone Bear, who has been to school a long time, and is the savant of the party, had brought his picture.  <span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">Mrs. K&auml;sebier's pictures of the Indians had shown them in all the beauty of strong brown faces, feather head dresses and bead jackets, but Sammy's picture was a marvel of civilized beauty.  It might even have been taken on the Bowery in New York, and it showed Sammy in practical if not really artistic, civilized attire</span></span>.</p>

		<p><span title="NA: Descriptions" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">This was a business suit, with a sack coat, a slouch hat, and a handkerchief brought around under Sammy's long hair, which hung over his shoulders, and tied in a big bow in front.  Then on a chair beside him hung a nice new overcoat, neatly folded.  In fact, all but the long hair, Sammy might have been a gentleman from the Bowery and a very neat, trim looking gentleman, too.  The picture had an Indian frame, all of beads and pointed at the top, and the recipient was more pleased than even Sammy could have imagined</span></span>.</p>

		<p>There is no strict rule of Mme. Grundy's book of etiquette for the entertainment of Indians at a studio reception, and it must have been a natural and appropriate gravitation that set them to drawing.  <span title="NA: Descriptions" style="background-color: transparent;">There were some wonderful Egyptian-like pictures that they drew&mdash;men and women with small heads and stiffly outlined bodies.  The sentiments of some of the pictures were, however, quite modern and up-to-date</span>.</p>

		<p>The one for instance that Joe Black Fox had made had printed the words: &quot;Catch Girl.&quot;  This presented a blanketed Indian Chief evidently in pursuit of his ladylove, a somewhat unattractive-looking young woman with a marvelously small head, who was a little in advance of him and presumably trying to run away.  She did not succeed, however, for the next picture, which was not marked, possibly because of the lack of sufficient erudition on the part of the artist, showed the &quot;Girl Caught.&quot;  There was the same Indian and the same blanket, but it sheltered two people, and above it could be seen the abnormally small head of the pursued maiden.</p>

		<p><span title="NA: Character" style="background-color: transparent;">It is possible that Joe Black Fox has a pretty fiancee at home, in Indian land, which would account for his interest in affairs of the heart, even those of other people</span>.  To one of the entertainers at the studio party&mdash;a pretty young matron who did much to make the say of the guests pleasant&mdash;he made a confidential communication. It was she who had presented him with the violets and done much for the entertainment of the big Chief, Iron Tail, who enjoyed the hospitality exceedingly. Black Fox drew his entertainer aside and told her impressively:</p>

		<p>&quot;Iron Tail like you. He have big stockingful money.&quot;</p>

		<p>That was interesting for two reasons&mdash;as <span title="NA: Character" style="background-color: transparent;"><span title="Image: Civilization" style="background-color: transparent;">showing the state of Iron Tail's feelings, and also that to hoard money in a stocking is one of the first principles of civilization which appeals to the aboriginal</span></span>.</p>

		<p>But it was <span title="Image: Civilization" style="background-color: transparent;">Philip Standing Soldier who exhibited the true and spontaneous feelings of a gentleman</span>.  At the studio reception the red&ndash;men guests had each of them presented as a special souvenir to some one of the entertainers a long feather from the big feathered head dresses.  The young woman to whom Philip had made this picturesque presentation had appreciate it so highly that when the night of the &quot;show&quot; party arrived arrived she had with her a big gold hat pin, with a big green stone in it, to present to him as a mark of her appreciation.  After the show, when the party went behind the scenes to say goodbye, the Indians all came downstairs from their quarters above, even the squaws, one with a papoose in her arms, and little Mary Lone Bear, Sammy the servant's little sister.</p>

		<p>Every one said &quot;How&quot; and &quot;Good-bye,&quot; and &quot;Washta,&quot; or something which sounds like that, which means &quot;How do you do&quot;; Iron Tail said &quot;Do,&quot; with a long sound of the &quot;o,&quot; an Indian affirmative in answer to everything said to him, and the girl who had received Philip's feather gave him the pin.</p>

		<p>Philip was pleased.  He showed it plainly, but he did not say anything.  He simply took a pretty little bag of porcupine quills, red, with an attractive little Indian design upon it, and handed it to the girl as a return gift.  All of which goes to show that <span title="Image: Civilization" style="background-color: transparent;">the Wild West Indian is not only instinctively a gallant, but a most appreciative gentleman</span>.</p>

			
