		<h1>WH&ndash;O&ndash;O&ndash;OP!  IRON TAIL AND HIS BRAVES COME</h1>

		<h3>But They Travel on to the Rising Sun This Morning.</h3>
		<h3>SOME 40 GOING TO FRANCE</h3>
		<h3>Buffalo Bill Will See Them Off&mdash;Mr. Edward Plenty Holes Expresses Some Pity</h3>

		<p><span title="NA: Members" style="background-color: transparent;">Chief Okiyap Sni, his little son Willie and Mrs. Okiyap Sni</span>, accompanied by <span title="NA: Members" style="background-color: transparent;">forty&ndash;three braves, four squaws, one other papoose</span> than Willie, and five Mexican cowpunchers, arrived in New York yesterday by the Erie from Cody, Wyoming, and grunted from 5'oclock in the afternoon, when they beheld the first tall building, until 11 o'clock last night, when Mrs. Okiyap accompanied her lord and master up the stairs of the International Hotel at Clarkson and West Streets.</p>

		<p><span title="Context: Custer Massacre" style="background-color: transparent;">The chief is none other than <span title="NA: Members" style="background-color: transparent;">Iron Tail</span>, one of the most famous of the then young braves who participated in the Custer massacre</span>.  <span title="NA: Character" style="background-color: transparent;">He is a silent and dignified man, with the hauteur that his prowess on the field of battle and the proper realization of his splendid American family tree should give him and with the majestic carriage becoming his superb raiment of beads, feathers, and paint</span>.  George Rose, the only later&ndash;day American in the bunch, and head cowpuncher for Col. Cody, was the chief's bodyguard.</p>

		<p>Chief Iron Tail and his Sioux braves, squaws, and papooses, will <span title="NA: Travel" style="background-color: transparent;">sail on the French liner La Gascogne this morning for Havre, and will be met at Versailles</span> by Jacob White Eyes, Buffalo Bill's chief interpreter, who married a French girl and is able to scramble on all fours through the French language.  Col. Cody himself accompanied the party from the town of his name to Chicago.  He will arrive here this morning and see that Iron Tail and his party sail.</p>

		<p><span title="Context: Custer Massacre" style="background-color: transparent;"><span title="NA: Members" style="background-color: transparent;">The Sioux in the party are all from the Pine Ridge Reservation and are known as the Oglalla Sioux</span>, that part of the tribe every man of which was in the death circle around Custer and his troops.  The variety of his is shown in the instant clamping of jaws and cunning glances the braves show when the name of Custer is mentioned</span>.</p>

		<p>One of the braves is <span title="NA: Members" style="background-color: transparent;">Chief Stinking Bear</span>, a noble specimen of humanity, with a blue and red ring painted around his left eye.  Another is <span title="NA: Members" style="background-color: transparent;">Edward Plenty Holes</span>, who frequently tripped on the edge of his blanket.  He alone consented to a few lines for publication.  He said that he could not understand why the pale faces should crawl around under the earth.</p>

		<p>It seemed cruel to him.  He had been investigating the Subway, and said that it was bad enough to have people fighting to get up in the air to squeeze into demon wagons, but it was awful to have them fighting to get under the earth.  <span title="Image: Civilization" style="background-color: transparent;">Mr. Plenty Holes was sore in his soul after a general view of the situation here, and said that he liked the pale faces too much to see them acting so unwisely</span>.  He preferred to walk, even if it was cold and he had nothing but moccasins.</p>

		<p><span title="NA: Members" style="background-color: transparent;">Mr. and Mrs. Henry Comes Out Holy</span>, other members of the party, retired early.  The little Early stood the journey well, they reported from upstairs.  <span title="NA: Members" style="background-color: transparent;">Willie Children, Understanding Crow, John Long Bull, (of no family connection with Sitting Bull,) Jealous Of Them, Max Red Ear Horse, Jefferson Strikes Plenty, James White Bear Claws, and two or three others</span> started to play cards but were discouraged and sent to bed.  The police had not reported any scalping party in West Street up to 2 o'clock this morning.</p>
			
