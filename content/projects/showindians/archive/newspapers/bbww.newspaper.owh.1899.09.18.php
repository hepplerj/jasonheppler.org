		<h1>RICH DAY FOR EXPOSITION</h1>

		<h3>Great Crowd Drawn to Ground by Its Beauty and Special Attractions Offered.</h3>
		<h3>Indians at Show Join With Those With Buffalo Bill's Combination In Startling War Dance.</h3>
		<h3>Pow-Wow Attracts Attention of the Multitude and Forms Interesting Display &ndash;&ndash; Two Magnificent Concerts.</h3>

		<p>One of the largest and most enthusiastic crowds of the season spent yesterday on the grounds of the Greater America exposition.  And the day was full of things to please and amuse.  There were for the sight-seers baseball, two great concerts, a red-hot Midway and an Indian dance and pow-wow on the East Tract.</p>

		<p><span title="NA: Performances" style="background-color: transparent;">The Indian pow&ndash;wow and dance</span> was something out of the ordinary.  When Buffalo Bill's Wild West Indians came in Saturday night the exposition Indians want to call on them, being members of the same tribe.  The red man spent some time visiting Saturday night, and early yesterday morning.  Superintendent Gaines' Indians went to the Wild West headquarters down town and spent the morning with their brethren.  <span title="NA: Descriptions" style="background-color: transparent;">Gifts were made of ponies, money, beadwork, blankets and clothing, after which a great camp dinner was served, at which fully 200 braves partook.  They had a big time fighting their battles over and discussing old times on the frontier.  Then the Wild West Indians visited the exposition grounds with the White City Indians and after a great supper all painted and rigged out in the most gorgeous manner possible, a warm war dance and general pow&ndash;wow was indulged in, witnessed by a very large audience</span>.  This was a great treat and a most interesting sight.</p>

		<p><span title="NA: Members" style="background-color: transparent;">Iron Tail was master of ceremonies for the Wild West Indians and Jack Red Cloud and Little Wound for the White City fellows</span>.  <span title="NA: Performances" style="background-color: transparent;">After the dance the Indians took in the Midway, receiving a very hearty welcome at almost every place</span>.  <span title="Image: Noble Savage" style="background-color: transparent;">It was a great day for the noble Americans.  They are all from the same agency and this was their first meeting for many months</span>.</p>

