		<h1>AROUND AN EAGLE FEATHER.</h1>

		<h3>A Token Which No Indian Warrior Touched Unless He Had Killed an Enemy.</h3>

		<p>At noon to&ndash;day the Indians of the Pawnee and Sioux nations and the group of Comanche Indians, who have been with the Wild West show at Erastina, S. I., were ready for their last dinner in the encampment before leaving for their homes on the reservations.  The men wore new clothes consisting of coats, pants and vests and white shirts, and had new shoes and broad brimmed hats.  Their long black hair had been neatly braided and paint washed from their faces.  All the men were thus attired, except Lena Wolf, chief of the Bad Face band of Sioux Indians, who could not be induced to cast off his scarlet blanket and native dress.  At dinner they were all seated in the same tent and were served with chicken fricassee, roast beef, roast mutton, potatoes, corn, beans, rice pudding, custard pie, tea and coffee.  At 3:30 p.m. they left on a rapid transit train for New York via St. George.  They go West by the Erie Railroad, each tribe having a special car, as the Sioux have a longer distance to travel than the Pawnees.</p>

		<p>During the &quot;Medicine&quot; dance performed by the Indians in honor of Buffalo Bill on Saturday night an eagle feather was planted in the center of the great tent where the Indians were, and there followed a wild dance about the feather, which no one dared to touch during the dance unless he had killed a man in battle and could prove it by some one present.  Rocky Bear, chief of the Ogallala band, touched it several times; so did American Horse, the warrior chief of the Sioux nation.  Lone Wolf touched the feather sixteen times and then plunged his arm into a kettle of boiling water from the bottom of which he took a beef bone.</p>

