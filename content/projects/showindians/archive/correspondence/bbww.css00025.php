			<h2>Letter from V. R. Day to John Brennan</h2>
			
			<p>
			December<br/>
			eighth<br />
			1913
			</p>

			<p>
			Major John Brennan<br/>
			Pine Ridge Agency, S. D.
			</p>

			<p>
			Dear Sir:&#8212;
			</p>

			<p>I am sending you a clipping from the Chicago Sunday Examiner, dated December 7th from a supposed interview at Pine Ridge Agency.</p>

			<p>Some one must be taking a lot of pains upon themselves to give us publicity.</p>

			<p>You know yourself that General Miles or Colonel Cody did not appear in the picture of the Wounded Knee Battle, but seemingly the Indians have an idea that all the pictures we took out there will work into the Wounded knee Battle, but you, I presume, understand that these various battles will appear separate and distinct, and the only thing that will not be authentic in the Wounded knee Battle will be the fact that the picture will not show the men and women being slaughtered, as they really were.</p>

			<p>The Indians knew that we were reproducing the Wounded Knee Battle and the interpreters informed them all that this was the Wounded knee Battle that we were producing, and Horn Cloud and Beard and others were opposed to it at the time but finally joined in.  Miles and Cody did not fake and appeared in nothing out there that they were not really in, except, I believe, Cody was in our review while at the final surrender he did not appear.</p>

			<p>If these Indians should go to Washington we would be pleased to have them stop in Chicago as our guests and we would show them, at the time, these pictures.  We might save them some money by ending their trip here rather than going to Washington under misapprehension.</p>

			<p>Yours very truly,<br/>
			VR Day</p>
			
