			<h2>Letter from William F. Cody to Charles H. Penney</h2>
			
			<p>
			North Platte, Neb<br/>
			Jan. 5th, [18]94
			</p>

			<p>
			Capt. Chas. H. Penney<br/>
			Agt. Pine Ridge<br/>
			S. Dak
			</p>

			<p>Dear Capt.</p>

			<p>Mr. Salsbury has just sent me the form of agreement for the Indians chosen by yourself. We both approve of it.  And just what we have wanted the Indians to for years. But as their contracts was up or made out this way before they wished their money as soon as the show closed. But this will be much better for them each and all will have a little money when they arrive at the Agency. <strike>this way</strike></p>

			<p>Accept our sincere thanks for your kindly interest.</p>

			<p>Wishing you a happily New Year.</p>

			<p>
			Very truly yours<br/>
			W. F. Cody
