			<h2>Letter from William F. Cody to George Leroy Brown</h2>
			
			<p>
			August 30 1892
			</p>

			<p>
			Capt Geo le Roy Brown<br />
			United States Indian Service<br />
			Pine Ridge Agency, S.D.
			</p>

			<p>My dear Captain,</p>

			<p>Your favor of August 9th to hand, which found us all well here. We are all going home in October, and the Indians will reach Pine Ridge Agency about the 1st of November. I lately sent two Indians home who were not sick, but who complained a little of lung complaint. I sent them home as I had plenty here without them.</p>

			<p>I have informed the Indians who have been sending money to write &amp; explain to you who the money was intended for. As they are going home so soon I hardly think they will send any more money to their relatives.</p>

			<p>
			Thanking you for past favors, <br />
			I am,<br />
			Very truly yours,<br />
			</p>
			<p>
			W. F. Cody
			</p>
			
