			<h2>Letter from Second Commissioner to John Brennan</h2>

			<p>JAN 10 1912</p>
			
			<p>
			Chief Red Cloud, et. al.,<br />
			Through Mr. John R. Brennan,<br />
			Superintendent Pine Ridge Indian School,<br />
			Pine Ridge, South Dakota.
			</p>
	
			<p>
			Gentlemen:
			</p>
			
			<p>Receipt is hereby acknowledged of your petition to the President, dated December 232, in regard to the death of the son of Chief Good Lance and the employment of Indians in &quot;wild west&quot; shows, and referred by him to this office for reply.</p>

			<p>The Office is in receipt of a report from the Superintendent of your reservation in which he states that the matter was taken up November 5 at a conference in his office at which a representative of Buffalo Bill, Old Good Lance, Red Cloud and others were present, and that an agreement which was thoroughly understood and satisfactory to all present was reached whereby arrangements should be made for the return of the body to the reservation early in the spring.</p>

			<p>The Office is doing all within its power to discourage the promiscuous employment of Indians for show purposes<!--page 2--> and is endeavoring in every way to induce them to settle on their allotments and become industrious, self-respecting citizens.</p>

			<p>There are, of course, some reputable exhibition companies in whose employment Indians who are not actively engaged in some profitable industrial pursuit on the reservations may derive advantages and before permitting representatives of show companies to enter any reservation under the jurisdiction of this Office a most satisfactory showing must be made concerning the character and methods of the company and the nature of the performances in which the Indians must engage.</p>

			<p>It has been the uniform practice to <span title="NA: Experiences" style="background-color: transparent;">require individual contracts with each Indian engaged, supported by a good and sufficient bond, guaranteeing payment of the salaries agreed upon, including all expenses from and returning to the reservation, proper care and attention in case of illness, and that an honest effort be made to protect the Indians from improper associations and prevent them from procuring intoxicating beverages</span>.</p>

			<p>The Office is endeavoring to dissuade Indians from nomadic habits and the neglect of their home interests, and you are assured of it support in furthering all practical<!--page 4--> means of removing from the lives of our Indians contact with evil associations and influences.</p>

			<p>Respectfully,<br/>
			Assistant Commissioner.
			</p>

			<p>
			Copy to Superintendent<br />
			Pine Ridge.
			</p>
	
			
