		<h2>Letter from William F. Cody to Charles H. Penney</h2>
			
		<p>
		North Platte, Neb. Feb 28th 1894
		</p>

		<p>
		Capt Charles Penney<br/>
		Act Agt Pine Ridge
		</p>

		<p>Dear Captain</p>

		<p>I presume you were informed by the Interior Dept. that I have permission for Indians. I am writing Shangrau and wish wish to commense looking up Indians. Until I have your permission so if your see Shangrau will you please speak to him about it. I will not want the Indians much before May. but its will to be letting them know. I will try my best to come after them my self&#8212;</p>

		<p>
		With my best wishes <br/>
		Yours truly<br />
		W. F. Cody
		</p>
		
