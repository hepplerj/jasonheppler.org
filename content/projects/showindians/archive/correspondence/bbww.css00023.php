			<h2>Letter from W. H. Clapp to William A. Jones</h2>
			
			<p>
			Department of the Interior<br/>
			U.S. INDIAN SERVICE
			</p>

			<p>
			Pine Ridge Agency, July 9, 1899.
			</p>

			<p>
			The Honorable Commissioner<br/>
			Of Indian Affairs,<br/>
			Washington, D. C.
			</p>

			<p>Sir:</p>

			<p>I have the honor to request that the following may be brought to the attention of the Honorable Secretary of the Interior, not as in any manner a criticism of action heretofore taken, but as being a statement of fact possibly useful when the subject again comes before the secretary for consideration.</p>

			<p>I refer to the increasing demand for Indians for show purposes.  Having been for some years at thisAgency, from which the Wild West show has recruited its Indians annually, I am in a position to judge of the effects of using them for exhibition purposes.  It is claimed by these desiring to employ these Indians that the opportunities afforded them to see what white men have done and are doing, to realize the resources of the country, both in numbers and in wealth, would educate the Indian and deter them from outbreaks, and that seeing the manner in which whites live, would stimulate him to adopt civilized modes of living.  To a limited extent this may have once been true, but is no longer so.  Ten or more years of such going about in all the cities of this country and many of those in Europe, together with numerous visits of delegations to Washington, and constant intercourse with neighboring towns, leave little either of good or bad, connected with the whites, for the Indians, (at least of his reservation), to learn.  The argument has therefore lost any force it may once have had.  In point of fact Indians are not desired by the show people, for any purpose but as an attraction, something to stimulate attendance and lure more half dollars into the treasury.  Now people will not be curious to see civilized Indians &ndash; those whom at great expense the Government has educated and to some extent civilized &ndash; none such are wanted, but only those who are yet distinctively barbarous, or who can pose as such.  All others are unwelcome and are denied employment.  The result is a premium upon barbarism.  It is in effect saying to the Indian: &quot;If you retain purely Indian customs, remain a savage with all the gand of feathers, naked bodies, hideous dancing, and other evidence of savagery, we want you, and should you have or can procure a dress trimmed with scalps, we want you very much, and will pay you accordingly.&quot;  The Indian is thus taught that savagery has a market value and is worth retaining.  The boys in the day schools know it, and speak longingly of the time when they will no longer be required to attend school, but can let their hair grow long, dance Omaha, and go off with shows.</p>

			<p>The influence of this sort of thing is far reaching and seriously retards progress.  In the interest of the Indians, whom we are striving to elevate, the Government should no longer permit these exhibitions of that which it is trying at so much expense to suppress.</p>

			<!--page 3-->

			<p>These shows are not instructive or beneficial to the whites, conveying as they do wrong ideas and impressions regarding the Indians, and leading many of them to think that all Indians are such as they see brought out at shows.  Such exhibitions have no higher effect than ministering to a morbid curiosity unworthy of civilized human beings.  People go to see naked painted Indians from quite the same motives as they do to see freaks: a two headed girl, or a six legged calf &ndash; but I maintain that no good is subserved, whether the exhibition is labled an &quot;Indian Congress&quot;, or a &quot;Wild West Show&quot;, but that on the contrary, the result is harmful to both whites and Indians; to the latter because by such means their civilization is retarded, and the efforts made for their advancement become a mockery.  The pleadings of missionaries and the zeal of those engaged in teaching, are alike futile among all those Indians who, under government sanction, are taught that continued barbarism is perhaps after all the best thing for them.</p>

			<p>Then also, the moral effect upon those Indians who are taken to exhibitions of whatever sort, is far from good.  in the greed for patronage and gain, all sorts of things are permitted and encouraged which ordinarily would be suppressed by the police.  Not one of these Expositions is now complete without its &quot;Midway&quot;, made up of scandalous, and suggestively immoral shows for the most part, and designedly pandering to the lowest passions.  The moral atmosphere about these places is fetid and impure.  Indians <!--page 4-->employed at them have much idle time, and like all others, are free to see all that is to be seen.  It is folly to suppose that they do not take advantage of their opportunities.  The season over, during which the car lines and the merchants have been enriched and the city boomed, poor Lo comes back to his home with an intimate knowledge of the seamy side of white civilization; his desire for change and excitement intensified, his all too faint aspirations for the benefits of civilization checked if not destroyed, and with a conviction that the boasted morality of the whites is nothing to be proud of or to copy.  The Agency Physician states that nearly all of the unnamable diseases now occuring on this reservation are traceable to those Indians who have returned from shows and Expositions.</p>

			<p>
			Very respectfully,<br/>
			Col. [unintelligible]<br/>
			Lieut. Colonel, U.S.A.,<br/>
			Acting Indian Agent.
			</p>
			
