		<h2>Letter from Nate Salsbury to Charles Penney</h2>
			
		<p>Brussels, May 8th, 1891</p>

		<p>
		Capt. Charles Penney <br/>
		Acting U.S. Indian Agent<br/>
		Pine Ridge Agency, S.D.
		</p>

		<p>Dear Sir</p>

		<p>Your letter to Col Cody is before me, and as the managing partner of the Col I make you the following statement which I hope will be a satisfactory answer to the question of Herbert Welsh.</p>

		<p>I find on <span title="NA: Experiences" style="background-color: transparent;">a cursory examination of our books</span> that we have paid to Sioux Indians since 1885 the sum of $74,300.  This sum does not include the wages we have also paid to Pawnee and Omaha Indians who have been in our employ.  Nor does it include any part of the expense for their transportation and maintainance<!--sic-->.</p>

		<p>You can also add to this sum the cost of a good substantial suit of clothes which we invariably present to each indian [sic] at the close of each season of service with us, and which is entirely outside of our contract with them.  We give the clothes as a sort of premium for good conduct and saving habits.</p>

		<p>I am sorry that no record has been kept of the money sent home by them.  I am confident such a record would more than substantiate my estimate that at least one third of their wages have been sent back to the Reservation.  Add to this, the sum that each man takes with him on discharge and I am sure the aggregate will reach one half of his wages.  For example, When the Indians left Pine Ridge this spring we became responsible for about $1200 worth of foods which they purchased from me.  Asay, Finlay &amp; Dawson for distribution among their families.  They have repaid this sum to us.  They have sent $200 home and they have credit with our Treasurer for $600. These sums practically represent $2,000 saved by the Indian in about two months. A pretty good shwoing I think to say nothing of the money they may have in their pockets.</p>

		<p>We do not pretend to control the disposition of their wages.  In the first place we have no shadow of a right to do so.  In the next place we find that an Indian knows the value of a dollar quite as well as a white man.  Of course we constanly [sic]<!--sic--> urge upon them the value of saving habits and we exercise a constant vigilance that they do not fall into hands of tradesmen who are sharpers, This is hardly necessary as we find that they have a good knowledge of relative values as any Jew or Gentile with whom they may come in contact.</p>

		<p>They transmit their money home through the various agencies of Post Office Orders.  Bank checks and express companies and as each man is a free agent in his personal affairs we have no means of knowing what sums they send without direct inquiry of them.  This inquiry we will not make as it would be a direct violation of our rule to treat them with the same personal consideration as we do our white employees.  We believe that the application of this rule adds to their self esteem and dignifies the relations between us.</p>

		<p>It would interest you, I know, to be present on a pay day to note the careful counting each man gives to his account.  They are honorable to a degree in paying their debts and equally careful to get all that belongs to them.  Some of them going so far as to enquire the rate of exchange between a foreign coin and the American dollar.  I assume that any man who can exercise such judgment is quite capable of choosing his occupation and profiting by it.</p>

		<p>In conclusion sir, I am at your service regarding any information specific or otherwise that you may desire to have about our Indians.  Your letter betrays a keener knowledge of Indian character than I have even known to be expressed in so few words.  Any real friend of the Indians in our employ can have any information he desires regarding these.  We only resent the impertinence and abuse of people who know no more about the real character of an Indian than they do about a degraded Hottentot.</p>

		<p>
		I have the honor sir to remain<br/>
		yours very truly<br/>
		Nate Salsbury<br/>
		Director<br/>
		Buffalo Bill's Wild West Co&#8212;
		</p>
		
