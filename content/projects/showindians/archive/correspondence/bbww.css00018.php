		<h2>Letter from Iron Tail to John Brennan</h2>

      	<p>
		Exmouth, England.<br/>
		May 27th, 1904.
		</p>

		<p>
		Major J. R. Brenan,<br/>
		Dear Sir: &#8212;
		</p>

		<p>I will now write few lines to let you know we are getting along very nice with the Show.  Good food and clothing and every thing.  Major, I want you to hold my boy's, Philip Iron Tail, money, until I come again.  I think I got right to save some for the Little Ones.  Don't you see they would spent in a day or two, of course we Indians don't know how to handle money, so if you give the widow all at once, her parents get hold of it and spent on their self, so I like you to be Gardian of the money.</p>

		<p>Hold the money until I come again, then we can see what we will do it. Now will close this time. We are all fat and healthy because we are under good care. Answer soon.</p>

		<p>Glad to shake hands with you.</p>

		<p>Chief Iron Tail</p>
		
