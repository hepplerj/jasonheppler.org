			<h2>Letter from William F. Cody to John R. Brennan</h2>
			
			<p>
			Glen Falls <br/>
			June 18th [19]09
			</p>

			<p>Dear Col. Brennan,</p>

			<p>Pardon me for forgetting to send the $1.00 for the telegram.</p>

			<p>We are getting along nicely. These Brules haven't the snap and get up that the Oglallas have but they do fairly well. And they work for about half the wages. We paid the Ogalallas [...] amounts to a big lot of money on the seasons.</p>

			<p>Please remember me to Iron Tail or any of my red brothers.</p>

			<p>
			With my very kindest regards to your self,<br />
			Yours truly, <br />
			W. F. Cody
            </p>
			
